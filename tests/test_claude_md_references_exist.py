"""CLAUDE.md ссылается только на существующее, и ни один страж не остался безымянным.

ЗАЧЕМ. Правила держат короткими приёмом «правило, которое принуждает машина, описываем
одной строкой и ссылаемся на механизм». Цена приёма — молчаливое протухание: механизм
переименовали, перенесли или сняли, а строка осталась. Читатель (модель следующей
сессии) верит, что защита есть, а её уже нет — худший вид защиты, дающий ложную
уверенность.

ЗАМЕР, РАДИ КОТОРОГО НАПИСАНО (2026-07-24): `work_snapshot.sh` был зарегистрирован в
`.claude/settings.json` и лежал на диске, но в перечне «Исполняется машиной» его не
было — страж есть, строки нет. Ни один прежний тест этого не видел:
`tests/test_hooks_wiring.py` сверяет настройки с диском и файла правил не читает вовсе.

ЧТО ЛОВИТ
1. Путь репозитория в обратных кавычках, которому не соответствует ни один файл, —
   в том числе путь ВНУТРИ команды запуска и путь с пробелами.
2. Хук, зарегистрированный в настройках, которого нет на диске.
3. Хук на диске, который не зарегистрирован и не назван в правилах, — снятый страж,
   читающийся как живой.
4. Хук, который ЗАРЕГИСТРИРОВАН, но в правилах не назван ни разу: правила утверждают,
   что перечисляют все механизмы, и умалчивание делает этот перечень ложным.
5. Вырождение самой проверки в пустую (анти-ржавчина).

ЧЕГО НЕ ЛОВИТ. Механизм, который жив, но делает уже не то, о чём написано в правиле.
Машина сверяет существование, а не смысл, — это зона человека.

НАПРАВЛЕНИЕ ОТКАЗА
• Всё, что лежит в git, — fail-CLOSED: нет файла → тест красный.
• Путь ВНЕ git в рабочей ветке физически отсутствует, и его присутствие непостоянно
  даже между копиями одной машины. Отсутствующий игнорируемый путь — пропуск, но
  пропуск ПРЕДУПРЕЖДЕНИЕМ, а не молчанием.
• Набор стражей: fail-CLOSED по НЕПОНЯТНОМУ — хук, из команды которого не вытаскивается
  путь скрипта, роняет тест, а не пропускается.
• Сам файл правил в этом репозитории ИГНОРИРУЕТСЯ git и до CI не доезжает — там
  проверка пропускается явно. Пропуск виден в сводке pytest.
"""
from __future__ import annotations

import json
import re
import subprocess
import warnings
from pathlib import Path

import pytest

_ROOT = Path(__file__).resolve().parents[1]
_CLAUDE_MD = _ROOT / "CLAUDE.md"
_SETTINGS = _ROOT / ".claude" / "settings.json"
_HOOKS_DIR = _ROOT / ".claude" / "hooks"

# Каталоги верхнего уровня, ссылки на которые сверяются. Перечислять можно и те, что
# игнорируются git: отсутствие такого пути даёт предупреждение, а не падение. Каталог,
# которого нет в списке, не проверяется вовсе — пропуск молчаливый, поэтому список
# полнее лучше.
_TOP_DIRS = ("claude_memory", "tests", "examples", "hooks", "assets",
             ".claude", ".github", ".githooks")

# Каталог механизмов: на ссылках на него держится таблица «что правишь → что гонять».
# Нужен анти-ржавчине — если ссылки на него исчезли, сломан разбор или сама таблица.
_MECHANISM_DIR = "tests/"

# Имя стража в обратных кавычках без пути. В этом репозитории стражей принято писать
# путём, и такая ссылка проверяется разбором путей; проверка ниже — страховка на случай
# голого имени. Суффиксы перечислены ЯВНО: `_check` сюда не входит намеренно, под него
# попал бы модуль `stop_check` пакета, который хуком не является. Совпадения с именами
# модулей отсеиваются отдельно — список модулей читается с диска, а не пишется руками.
_HOOK_NAME_RE = re.compile(r"^[a-z][a-z0-9_]*_(guard|notice|reminder|snapshot|audit)$")

# Команда хука, которая ничего не запускает, а сама себе сообщение. Список ЗАКРЫТЫЙ:
# всё, что не отсюда и не содержит пути скрипта, роняет проверку.
_INLINE_HOOK_VERBS = ("echo", "printf", "true", ":")

# Пороги анти-ржавчины. Поставлены ЗАМЕРОМ по живому файлу с запасом вниз: смысл порога —
# поймать сломанный разбор, а не отредактированный файл правил.
_MIN_PATHS = 15
_MIN_MECHANISM_PATHS = 5

_SPAN_RE = re.compile(r"`([^`\n]+)`")

# Пути, которых в этом репозитории быть НЕ ДОЛЖНО и которые правила называют именно
# поэтому. Отличить «ссылку на исчезнувшее» от «заявления об отсутствии» по тексту
# нельзя, а угадывать по словам-отрицаниям — тот самый разбор чужого текста, на котором
# стражи ломаются. Поэтому список ЗАКРЫТЫЙ и с машинным сроком годности: отдельный тест
# требует, чтобы каждая запись была ВСЁ ЕЩЁ упомянута в правилах и ВСЁ ЕЩЁ отсутствовала
# на диске. Протухнуть молча он не может.
_ЗАЯВЛЕНЫ_ОТСУТСТВУЮЩИМИ = {
    ".claude/claude-memory.config.json":
        "движок в своём репозитории намеренно не установлен — решение владельца",
}


def _skip_if_no_rules() -> None:
    if not _CLAUDE_MD.is_file():
        pytest.skip("CLAUDE.md вне git и в этой выкладке отсутствует — сверять нечего")


def _drop_dotslash(token: str) -> str:
    return token[2:] if token.startswith("./") else token


def _starts_with_top(token: str) -> bool:
    return any(token == d or token.startswith(d + "/") for d in _TOP_DIRS)


def _paths_in_span(span: str) -> list:
    """Пути внутри одного участка обратных кавычек.

    Участок бывает двух видов, и разбирать их одинаково нельзя:

    • ЧИСТЫЙ ПУТЬ берётся ЦЕЛИКОМ, даже с пробелами. Разрез по пробелу дал бы
      несуществующий огрызок — ложный красный, из-за которого проверку отключат.
    • КОМАНДА режется по пробелам, и берутся только куски, начинающиеся с каталога
      проекта. Без этого непроверенной остаётся именно ссылка на механизм: в правилах
      механизм почти всегда записан командой запуска, а не голым путём.

    Признак команды намеренно грубый и ошибается в безопасную сторону: лишний кусок
    отсеется фильтром, а разрезанный путь с пробелами дал бы ложное падение.
    """
    s = span.strip()
    if not s:
        return []
    tokens = [_drop_dotslash(t) for t in s.split()]
    looks_like_command = len(tokens) > 1 and (
        not _starts_with_top(tokens[0])
        or any(t.startswith("-") for t in tokens[1:])
        or bool(re.search(r"[&|><$\"'`]", s))
    )
    if not looks_like_command:
        whole = _drop_dotslash(s)
        return [whole] if _starts_with_top(whole) else []
    return [t for t in tokens if _starts_with_top(t)]


def _normalize(token: str) -> str:
    """Хвостовая пунктуация и указание символа внутри файла: `claude_memory/x.py:func`."""
    token = token.rstrip(".,;:")
    if ":" in token:
        token = token.split(":", 1)[0]
    return token


def _exists(token: str) -> bool:
    if token.endswith("/**"):
        # `x/**` = «каталог со всем содержимым»: пустой каталог такую ссылку не
        # оправдывает. Отдельным случаем, потому что поведение Path.glob('x/**')
        # менялось между версиями Python и полагаться на него нельзя.
        base = _ROOT / token[: -len("/**")]
        return base.is_dir() and any(base.iterdir())
    if "*" in token:
        return any(_ROOT.glob(token))
    return (_ROOT / token).exists()


def _git_ignored(tokens: list) -> set:
    """Какие из путей git считает игнорируемыми — одним вызовом.

    Спрашиваем сам git, а не держим список в коде: список разъедется с .gitignore молча.
    Не репозиторий / нет git → пустое множество, и тогда каждый отсутствующий путь
    считается ошибкой (fail-closed).

    `core.quotePath=false` обязателен: иначе git отдаёт не-ASCII имена экранированными,
    ответ не сходится с исходным путём, и путь с кириллицей уезжает в ошибки.
    """
    if not tokens:
        return set()
    try:
        res = subprocess.run(
            ["git", "-C", str(_ROOT), "-c", "core.quotePath=false", "check-ignore", "--stdin"],
            input="\n".join(tokens), capture_output=True, text=True, timeout=15,
        )
    except (OSError, subprocess.SubprocessError):
        return set()
    # Код возврата 1 — «ни один путь не игнорируется»: это норма, не сбой.
    return {line.strip() for line in res.stdout.splitlines() if line.strip()}


def _collect_refs() -> list:
    """Все ссылки на пути: список пар (номер строки, путь)."""
    refs = []
    for n, line in enumerate(_CLAUDE_MD.read_text(encoding="utf-8").splitlines(), 1):
        for span in _SPAN_RE.findall(line):
            for raw in _paths_in_span(span):
                if "<" in raw:          # плейсхолдер вида `docs/<слаг>.md` — шаблон, не путь
                    continue
                refs.append((n, _normalize(raw)))
    return refs


def test_claude_md_references_exist() -> None:
    """Каждый путь репозитория, упомянутый в CLAUDE.md, существует."""
    _skip_if_no_rules()
    refs = _collect_refs()
    missing = [(n, t) for n, t in refs if not _exists(t)]
    ignored = _git_ignored(sorted({t for _, t in missing}))

    offenders = [f"CLAUDE.md:{n} → {t}" for n, t in missing
                 if t not in ignored and t not in _ЗАЯВЛЕНЫ_ОТСУТСТВУЮЩИМИ]
    skipped = [f"CLAUDE.md:{n} → {t}" for n, t in missing if t in ignored]

    if skipped:
        # Предупреждение, а не молчание: пропуск, о котором никто не узнал, — это дыра,
        # а pytest показывает предупреждения отдельной сводкой в конце прогона.
        warnings.warn(
            "CLAUDE.md ссылается на пути вне git — в этой выкладке их нет, проверить "
            "нечем (это не ошибка):\n  " + "\n  ".join(skipped), UserWarning, stacklevel=2)

    assert not offenders, (
        "CLAUDE.md ссылается на несуществующие файлы. Либо путь переименован и строку "
        "надо обновить, либо защита, на которую ссылается инструкция, удалена — тогда "
        "правило вернуть текстом или восстановить механизм:\n  " + "\n  ".join(offenders))


def test_разбор_вообще_что_то_находит() -> None:
    """Анти-ржавчина: сломанный разбор дал бы вечно-зелёный пустой тест.

    То есть ровно ту ложную уверенность, против которой весь файл и написан.
    """
    _skip_if_no_rules()
    refs = _collect_refs()
    assert len(refs) >= _MIN_PATHS, (
        f"в CLAUDE.md найдено лишь {len(refs)} путей — вероятно, сломан разбор "
        "(_paths_in_span / _TOP_DIRS) или файл потерял таблицу ворот")

    механизмы = [t for _, t in refs if t.startswith(_MECHANISM_DIR)]
    assert len(механизмы) >= _MIN_MECHANISM_PATHS, (
        f"в CLAUDE.md осталось лишь {len(механизмы)} ссылок на {_MECHANISM_DIR} — "
        "таблица «что правишь → что гонять» потеряна или сломан разбор")

    ignored = _git_ignored(sorted({t for _, t in refs if not _exists(t)}))
    проверенные = [t for _, t in refs if _exists(t) or t not in ignored]
    assert проверенные, (
        "все найденные пути оказались вне git — проверка выродилась в пустую")


@pytest.mark.parametrize("путь", sorted(_ЗАЯВЛЕНЫ_ОТСУТСТВУЮЩИМИ))
def test_исключения_не_протухли(путь: str) -> None:
    """Срок годности списка исключений держит машина, а не память.

    Исключение перестаёт быть верным двумя способами, и оба молчаливые: файл появился
    (значит правило устарело) или упоминание из правил ушло (значит запись лишняя и
    прикрывает уже другой путь).
    """
    _skip_if_no_rules()
    причина = _ЗАЯВЛЕНЫ_ОТСУТСТВУЮЩИМИ[путь]

    assert not (_ROOT / путь).exists(), (
        f"{путь} теперь существует, а исключение говорит «{причина}» — либо файл лишний, "
        "либо запись в списке устарела")
    assert путь in _CLAUDE_MD.read_text(encoding="utf-8"), (
        f"{путь} больше не упоминается в CLAUDE.md — запись в списке исключений лишняя")


def _зарегистрированные() -> dict:
    """Имя файла хука → событие. Fail-closed по непонятному."""
    настройки = json.loads(_SETTINGS.read_text(encoding="utf-8"))
    итог = {}
    for событие, группы in (настройки.get("hooks") or {}).items():
        for группа in группы or []:
            for хук in группа.get("hooks") or []:
                if хук.get("type") != "command" or not isinstance(хук.get("command"), str):
                    pytest.fail(
                        f"{событие}: хук не вида \"command\" — проверка его не понимает. "
                        "Вердикт «ОК» у непонятого конфига означает не «цел», а «я не "
                        "разобрал», и это разные вещи.")
                куски = хук["command"].split()
                скрипт = next((t for t in куски if t.endswith(".sh")), None)
                if скрипт is None:
                    if куски and куски[0] in _INLINE_HOOK_VERBS:
                        continue        # хук, который сам себе сообщение, законен
                    pytest.fail(f"{событие}: в команде хука не найден путь скрипта: {хук['command']}")
                # Берём ИМЯ ФАЙЛА, а не путь: путь там раскрывается переменной проекта.
                итог[Path(скрипт).name] = событие
    return итог


def test_набор_стражей_сходится_с_правилами() -> None:
    """Правила и набор механизмов сверяются в ОБЕ стороны.

    Четыре сверки в одном тесте, потому что они об одном: соответствие правил набору
    механизмов. Разнести их значило бы четыре раза перечитать одни и те же два файла.
    """
    _skip_if_no_rules()
    if not _SETTINGS.exists() or not _HOOKS_DIR.is_dir():
        pytest.skip("нет .claude/settings.json или .claude/hooks — сверять нечего")

    текст = _CLAUDE_MD.read_text(encoding="utf-8")
    зарегистрированные = _зарегистрированные()
    проблемы = []

    # (а) Зарегистрированный хук обязан лежать на диске: иначе среда молча спотыкается
    #     о ссылку на несуществующий скрипт при каждом срабатывании.
    for имя, событие in sorted(зарегистрированные.items()):
        if not (_HOOKS_DIR / имя).exists():
            проблемы.append(f"{событие}: зарегистрирован хук {имя}, но .claude/hooks/{имя} нет")

    # (б) Хук на диске обязан быть либо зарегистрирован, либо назван в правилах.
    #     Снятый с регистрации страж, о котором правила молчат, читается как живой.
    for путь in sorted(_HOOKS_DIR.glob("*.sh")):
        if путь.name in зарегистрированные:
            continue
        if путь.stem not in текст:
            проблемы.append(
                f"{путь.name} лежит в .claude/hooks/, не зарегистрирован и не назван "
                "в CLAUDE.md — снятый страж читается как живой")

    # (в) Страж, названный по ИМЕНИ без пути. Такая ссылка протухает так же молча, но
    #     разбором путей не видна вовсе. Имена модулей пакета отсеиваются: список
    #     читается с диска, поэтому новый модуль не потребует правки этого теста.
    for span in _SPAN_RE.findall(текст):
        имя = span.strip()
        if not _HOOK_NAME_RE.match(имя):
            continue
        if (_ROOT / "claude_memory" / f"{имя}.py").exists():
            continue                     # это модуль пакета, а не хук
        if not (_HOOKS_DIR / f"{имя}.sh").exists():
            проблемы.append(f"CLAUDE.md называет стража {имя}, но .claude/hooks/{имя}.sh нет")

    # (г) Зарегистрированный страж обязан быть НАЗВАН в правилах. Правила утверждают,
    #     что перечисляют все механизмы; страж, о котором они молчат, делает этот
    #     перечень ложным — а ложный перечень хуже отсутствующего. Именно этот случай
    #     и был найден: `work_snapshot.sh` работал, а строки о нём не было.
    for имя in sorted(зарегистрированные):
        if Path(имя).stem not in текст:
            проблемы.append(
                f"{имя} зарегистрирован в settings.json, но в CLAUDE.md не назван — "
                "перечень механизмов в правилах неполон, а значит недостоверен")

    assert not проблемы, (
        "набор стражей разошёлся с тем, что написано в CLAUDE.md:\n  "
        + "\n  ".join(sorted(set(проблемы))))
