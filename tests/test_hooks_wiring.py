"""Страж, который не подключён, — это отсутствующий страж, и выглядит он так же.

Прецедент 0.17.0 (ревю-8): обе точки подключения были выдернуты, и весь набор
тестов остался зелёным — потому что тесты звали хук ПО ПУТИ, напрямую, а не так,
как его зовёт Claude Code. Проверка самого файла ничего не говорит о том, будет
ли он вызван.

Здесь сверяются два списка в ОБЕ стороны:
  • каждый страж в `.claude/hooks/` упомянут в `.claude/settings.json`;
  • каждая команда из `settings.json` указывает на существующий исполняемый файл.
Перечень не выписан руками ни с одной стороны — обе стороны читаются с диска,
поэтому новый страж попадает под проверку сам.
"""
import json
import re
from pathlib import Path

import pytest

КОРЕНЬ = Path(__file__).resolve().parents[1]
ХУКИ = КОРЕНЬ / ".claude" / "hooks"
НАСТРОЙКИ = КОРЕНЬ / ".claude" / "settings.json"


def _команды() -> list:
    данные = json.loads(НАСТРОЙКИ.read_text(encoding="utf-8"))
    out = []
    for событие, группы in (данные.get("hooks") or {}).items():
        for группа in группы:
            for хук in группа.get("hooks") or []:
                if хук.get("type") == "command" and хук.get("command"):
                    out.append((событие, хук["command"]))
    return out


def _путь_из_команды(команда: str) -> Path:
    """Путь к файлу стража из строки запуска, с раскрытием переменной проекта."""
    очищенная = команда.replace('"$CLAUDE_PROJECT_DIR"', str(КОРЕНЬ))
    очищенная = очищенная.replace("$CLAUDE_PROJECT_DIR", str(КОРЕНЬ)).strip().strip('"')
    return Path(re.split(r"\s+", очищенная)[0])


def test_настройки_разбираются() -> None:
    """Битый JSON настроек = все стражи молча выключены."""
    assert json.loads(НАСТРОЙКИ.read_text(encoding="utf-8")), "настройки пусты"
    assert _команды(), "в настройках не зарегистрировано ни одного стража"


@pytest.mark.parametrize("файл", sorted(ХУКИ.glob("*.sh")), ids=lambda p: p.name)
def test_каждый_страж_подключён(файл) -> None:
    """Файл стража лежит в репозитории → он обязан быть вызван настройками.

    Обратное — самый дорогой вид молчания: страж есть, тесты на него зелёные,
    а в работе он не срабатывает ни разу.
    """
    подключённые = {_путь_из_команды(к).name for _событие, к in _команды()}
    assert файл.name in подключённые, (
        f"{файл.name} не зарегистрирован в settings.json — он не будет вызван никогда")


@pytest.mark.parametrize("событие,команда", _команды(),
                         ids=[f"{с}:{_путь_из_команды(к).name}" for с, к in _команды()])
def test_каждая_команда_указывает_на_живой_файл(событие, команда) -> None:
    """Опечатка в пути = хук не запускается, и Claude Code об этом не скажет."""
    путь = _путь_из_команды(команда)
    assert путь.is_file(), f"{событие}: файла {путь} нет — хук молча не сработает"
    assert путь.stat().st_mode & 0o111, f"{событие}: {путь.name} не исполняемый"
