"""Страж разрушительных git-команд (`.claude/hooks/destructive_git_guard.sh`).

ПОЧЕМУ ТЕСТ НА ПРОПУСК ВАЖНЕЕ ТЕСТА НА БЛОК. Полностью сломанный страж (синтаксическая
ошибка) отдаёт код 2 — ровно тот, которым PreToolUse ОТКЛОНЯЕТ вызов, — и проходит все
проверки «блокирует ли он что надо» зелёными. Поэтому блок засчитывается ТОЛЬКО вместе с
текстом объяснения на stderr, а пропуск — ТОЛЬКО при пустом stderr.

ГЛАВНОЕ СВОЙСТВО СТРАЖА: он блокирует, лишь когда терять реально есть что. На чистом
дереве жёсткая синхронизация с origin законна, и мешать ей нельзя — обойдённый страж
хуже отсутствующего.

ЧТО ЗАКРЫВАЕТ ЭТОТ ФАЙЛ СВЕРХ ТОГО. Страж читал команду целиком и принимал УПОМИНАНИЕ
опасной команды за её выполнение: сообщение коммита, описывающее аварию, блокировалось
(поймано на живой работе 2026-07-19). Текст сообщения из проверки исключён — но ровно в
двух местах, где он не исполняется никогда. Тесты ниже держат обе границы: и что ложная
тревога ушла, и что вырезание не стало дырой.
"""
from __future__ import annotations

import json
import subprocess
from pathlib import Path

import pytest

HOOK = Path(__file__).resolve().parents[1] / ".claude" / "hooks" / "destructive_git_guard.sh"
ОТКАТ = "git check" + "out -- ."          # собрано, чтобы не сработать на собственном файле
СБРОС = "git reset --hard"


@pytest.fixture
def грязный(tmp_path):
    """Репозиторий с НЕсохранённой правкой — тут стражу есть что защищать."""
    subprocess.run(["git", "init", "-q", str(tmp_path)], check=True)
    f = tmp_path / "f.txt"
    f.write_text("a\n")
    subprocess.run(["git", "-C", str(tmp_path), "add", "-A"], check=True)
    subprocess.run(["git", "-C", str(tmp_path), "-c", "user.email=t@t", "-c", "user.name=t",
                    "commit", "-qm", "init"], check=True)
    f.write_text("b\n")                    # правка есть, коммита нет
    return tmp_path


@pytest.fixture
def чистый(tmp_path):
    """Репозиторий без несохранённого — терять нечего, страж обязан молчать."""
    subprocess.run(["git", "init", "-q", str(tmp_path)], check=True)
    (tmp_path / "f.txt").write_text("a\n")
    subprocess.run(["git", "-C", str(tmp_path), "add", "-A"], check=True)
    subprocess.run(["git", "-C", str(tmp_path), "-c", "user.email=t@t", "-c", "user.name=t",
                    "commit", "-qm", "init"], check=True)
    return tmp_path


def _блокирует(cmd: str, cwd: str) -> bool:
    """True — отклонено С ОБЪЯСНЕНИЕМ; False — пропущено МОЛЧА. Иное → провал теста."""
    p = subprocess.run(
        [str(HOOK)],
        input=json.dumps({"tool_name": "Bash", "cwd": cwd, "tool_input": {"command": cmd}}),
        capture_output=True, text=True, timeout=30,
    )
    if p.returncode == 2 and "Заблокировано" in p.stderr:
        return True
    if p.returncode == 0 and not p.stderr.strip():
        return False
    pytest.fail(f"страж сломан: код={p.returncode}, stderr={p.stderr[:300]!r}")


# ── Основное поведение ────────────────────────────────────────────────────────

@pytest.mark.parametrize("cmd", [ОТКАТ, СБРОС, "git stash", "git restore f.txt"])
def test_блокирует_когда_есть_что_терять(грязный, cmd) -> None:
    assert _блокирует(cmd, str(грязный))


@pytest.mark.parametrize("cmd", [ОТКАТ, СБРОС + " origin/main"])
def test_молчит_на_чистом_дереве(чистый, cmd) -> None:
    """Терять нечего — команда законна. Страж, мешающий зря, будет обойдён."""
    assert not _блокирует(cmd, str(чистый))


def test_молчит_на_безобидной_команде(грязный) -> None:
    assert not _блокирует("git status --porcelain", str(грязный))


# ── Полнота списка: одна форма на команду — это недосработка ──────────────────
# Замерено 2026-07-19: список описывал по ОДНОЙ форме каждой команды, и восемь
# разрушительных вызовов проходили насквозь. `git checkout -f` теряет правки ровно так
# же, как `git checkout -- .`, но под шаблон не попадал. Классика этого репозитория:
# страж по одной формулировке недосрабатывает молча.

@pytest.mark.parametrize("cmd", [
    "git " + "checkout -f",
    "git " + "checkout --force .",
    "git " + "checkout HEAD .",
    "git switch --discard" + "-changes main",
    "git rm -f f.txt",
    "git worktree remove --force ../копия",
])
def test_ловит_вторую_форму_той_же_команды(грязный, cmd) -> None:
    """Падают на коде до расширения списка: все шесть проходили насквозь."""
    assert _блокирует(cmd, str(грязный))


@pytest.mark.parametrize("cmd", [
    "git " + "checkout main",          # переключение ветки без -f правки не теряет
    "git switch main",                 # то же самое
    "git rm --cached f.txt",           # убирает из индекса, файл на диске остаётся
    "git branch -D tmp",               # теряет коммиты, а не рабочее дерево — другой класс
    "git log -f",                      # `-f` тут вообще не про force
])
def test_не_ловит_похожее_но_безопасное(грязный, cmd) -> None:
    """Тест на ПРОПУСК к расширению списка: ложная тревога = обойдённый страж.

    Каждая строка тут внешне похожа на разрушительную, но правок не теряет. Без этих
    проверок расширение шаблонов легко перестреливает — `-f` встречается у многих команд
    git и далеко не всегда означает `--force`.
    """
    assert not _блокирует(cmd, str(грязный))


def test_видит_чужой_репозиторий(грязный, tmp_path) -> None:
    """Самый опасный случай: команда адресована ДРУГОМУ репозиторию через -C."""
    посторонний = tmp_path.parent / "elsewhere"
    посторонний.mkdir(exist_ok=True)
    assert _блокирует(f"git -C {грязный} check" + "out -- .", str(посторонний))


def test_обход_по_согласованию(грязный) -> None:
    assert not _блокирует(ОТКАТ + "  # согласовано с владельцем", str(грязный))


# ── ИЗВЕСТНЫЙ ИЗЪЯН: цитата принимается за действие ───────────────────────────
# Страж читает текст команды целиком, поэтому УПОМИНАНИЕ опасной команды в сообщении
# коммита он блокирует. Это ложная тревога: обходится переписыванием фразы.
#
# ПОЧЕМУ НЕ ПОЧИНЕНО. Попытка вырезать текст сообщения перед проверкой была написана,
# протестирована и ОТКАЧЕНА 2026-07-19. Две волны адверсарного поиска нашли СЕМЬ разных
# способов обезоружить стража через это послабление, каждый по своей причине:
# заголовок heredoc внутри кавычек; незакрытый heredoc; закомментированный `# <<'EOF'`;
# слова «git commit» из комментария на строке `python3 - <<'PY'`; heredoc, тело которого
# исполняет bash, а не git; heredoc внутри heredoc; спаривание литеральных кавычек
# регуляркой `-m` через разрушительную команду. Все семь воспроизведены запуском: старый
# страж блокировал, послабление пропускало, работа терялась по-настоящему.
#
# Вывод, который здесь и закрепляется: разобрать shell регулярным выражением нельзя, а
# цена ошибки несимметрична — ложная тревога стоит переписанной фразы, дыра стоит рабочего
# дня. Поэтому изъян принят ОСОЗНАННО, как в 0.16.0 для стража публикации.
#
# Тесты ниже закрепляют изъян КАК ЕСТЬ. Они не одобряют его: если кто-то найдёт решение,
# не опирающееся на разбор текста, они упадут и заставят переписать это объяснение, а не
# тихо разойтись с реальностью.

def test_упоминание_в_сообщении_коммита_блокируется(грязный) -> None:
    """ГРАНИЦА: сообщение, ОПИСЫВАЮЩЕЕ аварию, страж принимает за саму аварию.

    Поймано на живой работе: коммит про установку самого стража не проходил, потому что
    в тексте назывались опасные команды. Лечится переписыванием фразы.
    """
    cmd = (
        "git add -A && git commit -q -F - <<'MSG'\n"
        "chore: страж git-команд\n\n"
        f"Авария: суб-агент выполнил `{ОТКАТ}` и стёр работу.\n"
        "MSG"
    )
    assert _блокирует(cmd, str(грязный))


def test_упоминание_в_ключе_m_блокируется(грязный) -> None:
    """ГРАНИЦА: то же самое для короткого сообщения через `-m`."""
    assert _блокирует(f'git commit -m "описываю аварию: {ОТКАТ} снёс работу"', str(грязный))


# ── Дыры, которые дало бы послабление (закреплены, чтобы не вернуть их снова) ──
# Каждый случай ниже страж обязан блокировать. На откаченной правке они проходили и
# работа терялась. Если кто-то возьмётся вырезать текст сообщения заново — эти тесты
# покраснеют раньше, чем правка доедет до боевого дерева.

@pytest.mark.parametrize("cmd,почему", [
    ('echo "докой про git commit <<\'MSG\'"\n' + ОТКАТ,
     "заголовок heredoc внутри кавычек — это текст, а не открытие"),
    ("cat <<'OUTER'\ngit commit <<'MSG'\nOUTER\n" + ОТКАТ,
     "heredoc внутри heredoc: внутренняя строка — тело, а не заголовок"),
    (f"git commit -m 'wip' # <<'EOF'\n{СБРОС} HEAD\nEOF",
     "закомментированный заголовок: bash его не видит, а разбор видел"),
    (f"python3 - <<'PY'  # хелпер для git commit\nimport subprocess\n"
     f"subprocess.run('{ОТКАТ}', shell=True)\nPY",
     "слова git и commit пришли из комментария, тело исполняет python"),
    (f"git commit -m 'wip' && bash <<'SH'\n{СБРОС} HEAD\nSH",
     "тело heredoc исполняет bash, а не git commit"),
    (f"git commit -m 'wip'\necho '-m \"'; {СБРОС}; echo '\"'",
     "регулярка -m спарила литеральные кавычки через разрушительную команду"),
])
def test_обезоруживание_через_текст_сообщения_невозможно(грязный, cmd, почему) -> None:
    assert _блокирует(cmd, str(грязный)), почему


def test_страж_не_молчит_на_мусоре() -> None:
    """Fail-open по построению: непонятный ввод пропускаем, но БЕЗ шума на stderr."""
    p = subprocess.run([str(HOOK)], input="не json", capture_output=True, text=True, timeout=30)
    assert p.returncode == 0 and not p.stderr.strip()
