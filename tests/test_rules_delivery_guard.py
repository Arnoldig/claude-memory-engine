# ── Файл ОТСЛЕЖИВАЕТСЯ git: не трогаем, только говорим ──────────────────────

def test_отслеживаемые_правила_не_подменяются_ссылкой(tmp_path) -> None:
    """Ссылка поверх отслеживаемого файла ломает репозиторий у всех.

    Замерено в одноразовом репозитории: `git status` → ` T`, первый `git add -A`
    коммитит ссылку, чистый клон получает ссылку наружу репозитория.
    """
    корень = _корень(tmp_path, отслеживать_правила=True)
    копия = _копия(корень)

    вывод = _запустить(str(копия))

    assert not (копия / "CLAUDE.md").is_symlink(), "отслеживаемый файл подменять нельзя"
    assert (копия / "CLAUDE.md").read_text() == "ПРАВИЛА ПРОЕКТА\n"
    assert _git(копия, "status", "--short") == "", "ветка обязана остаться чистой"
    assert "правила проекта" not in вывод, "git доставил их сам — говорить не о чем"


def test_отслеживаемый_файл_ставший_ссылкой_НАЗВАН(tmp_path) -> None:
    """Сломанное состояние, которое молчаливо принимали.

    Ссылка на месте отслеживаемого файла читается исправно и выглядит нормально,
    а git уже видит смену типа. Промолчать здесь — значит дождаться коммита.
    """
    корень = _корень(tmp_path, отслеживать_правила=True)
    копия = _копия(корень)
    (копия / "CLAUDE.md").unlink()
    (копия / "CLAUDE.md").symlink_to("../../../CLAUDE.md")

    вывод = _запустить(str(копия))

    assert "смену типа" in вывод, "молчание здесь неотличимо от исправного состояния"
    assert (копия / "CLAUDE.md").is_symlink(), "чинить самим нельзя: это чужая правка"


def test_отслеживаемый_файл_разошедшийся_назван_и_не_тронут(tmp_path) -> None:
    корень = _корень(tmp_path, отслеживать_правила=True)
    копия = _копия(корень)
    (копия / "CLAUDE.md").write_text("СТАРЫЕ ПРАВИЛА\n", encoding="utf-8")

    вывод = _запустить(str(копия))

    assert "РАЗОШ" in вывод.upper()
    assert (копия / "CLAUDE.md").read_text() == "СТАРЫЕ ПРАВИЛА\n"
    assert not (копия / "CLAUDE.md").is_symlink()


def test_без_ответа_git_ссылку_не_делаем(tmp_path) -> None:
    """Не репозиторий → «версионирован» по умолчанию.

    Направление отказа выбрано ценой ошибки: лишняя строка в выводе стоит ничего,
    лишняя ссылка — сломанного репозитория.
    """
    корень = tmp_path / "проект"
    (корень / ".claude").mkdir(parents=True)
    (корень / "CLAUDE.md").write_text("ПРАВИЛА ПРОЕКТА\n", encoding="utf-8")
    копия = корень / ".claude" / "worktrees" / "wt"
    копия.mkdir(parents=True)

    вывод = _запустить(str(копия))

    assert not (копия / "CLAUDE.md").exists(), "без ответа git ссылку делать нельзя"
    assert вывод.strip(), "и молчать тоже нельзя: правил в ветке нет"


# ── Fail-open ───────────────────────────────────────────────────────────────

@pytest.mark.parametrize("ввод", ["не json", "", "null", '{"cwd": "/нет/такого"}', "[]"])
def test_мусор_на_входе_проходит_молча(ввод) -> None:
    """ПАРНЫЙ на пропуск: fail-open. Ошибка стража не смеет ронять старт сессии."""
    p = subprocess.run([str(HOOK)], input=ввод, capture_output=True, text=True, timeout=30)
    assert p.returncode == 0
    assert p.stdout == ""
