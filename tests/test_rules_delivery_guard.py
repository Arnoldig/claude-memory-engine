"""Доставка правил в рабочую копию: страж чинит её и говорит, иначе молчит.

Половина случаев здесь — на ПРОПУСК, и это не вежливость к коду, а требование:
страж, который шумит без повода, отключают вместе с настоящими находками, а
страж, молчащий всегда, неотличим от исправного. Поэтому у каждого срабатывания
есть парный случай, где вывод обязан быть пустым.

Проверяется поведение, а не текст сообщений: сверяем факт (появилась ссылка,
копия осталась нетронутой), а слова — лишь по опорному куску.
"""
import json
import subprocess
from pathlib import Path

import pytest

HOOK = Path(__file__).resolve().parents[1] / ".claude" / "hooks" / "rules_delivery_guard.sh"


def _запустить(cwd: str) -> str:
    p = subprocess.run([str(HOOK)], input=json.dumps({"cwd": cwd}),
                       capture_output=True, text=True, timeout=30)
    assert p.returncode == 0, "SessionStart не имеет права ронять старт сессии"
    return p.stdout


def _корень(tmp_path: Path, слова: bool = True) -> Path:
    корень = tmp_path / "проект"
    (корень / ".claude").mkdir(parents=True)
    (корень / "CLAUDE.md").write_text("ПРАВИЛА ПРОЕКТА\n", encoding="utf-8")
    if слова:
        (корень / ".claude" / "private-words.txt").write_text("слово\n", encoding="utf-8")
    return корень


def _копия(корень: Path, имя: str = "wt") -> Path:
    копия = корень / ".claude" / "worktrees" / имя
    копия.mkdir(parents=True)
    return копия


def test_доставляет_когда_правил_нет(tmp_path) -> None:
    корень = _корень(tmp_path)
    копия = _копия(корень)

    вывод = _запустить(str(копия))

    assert (копия / "CLAUDE.md").is_symlink(), "правила должны приехать ССЫЛКОЙ, не копией"
    assert (копия / "CLAUDE.md").read_text() == "ПРАВИЛА ПРОЕКТА\n"
    assert "правила проекта" in вывод, "молчаливая доставка неотличима от её отсутствия"


def test_молчит_когда_всё_на_месте(tmp_path) -> None:
    """ПАРНЫЙ на пропуск: второй старт в той же копии обязан пройти беззвучно."""
    корень = _корень(tmp_path)
    копия = _копия(корень)
    _запустить(str(копия))

    assert _запустить(str(копия)) == "", "страж шумит на исправной копии — его отключат"


def test_молчит_вне_рабочей_копии(tmp_path) -> None:
    """ПАРНЫЙ на пропуск: в главной папке файл настоящий, чинить нечего."""
    корень = _корень(tmp_path)

    assert _запустить(str(корень)) == ""
    assert not (корень / "CLAUDE.md").is_symlink(), "основной файл трогать нельзя"


def test_молчит_когда_доставлять_нечего(tmp_path) -> None:
    """ПАРНЫЙ на пропуск: нет правил в самом проекте — не выдумывать их."""
    корень = tmp_path / "проект"
    (корень / ".claude").mkdir(parents=True)
    копия = _копия(корень)

    assert _запустить(str(копия)) == ""
    assert not (копия / "CLAUDE.md").exists()


def test_копия_совпадающая_с_основным_становится_ссылкой(tmp_path) -> None:
    """Совпадает сегодня — разойдётся завтра. Ссылка разойтись не может."""
    корень = _корень(tmp_path)
    копия = _копия(корень)
    (копия / "CLAUDE.md").write_text("ПРАВИЛА ПРОЕКТА\n", encoding="utf-8")

    вывод = _запустить(str(копия))

    assert (копия / "CLAUDE.md").is_symlink()
    assert "ссылк" in вывод


def test_разошедшаяся_копия_названа_и_не_тронута(tmp_path) -> None:
    """Главный случай: замерен 2026-07-20 — копия правил на 3 КБ короче основной.

    Молча заменить её нельзя: расхождение может быть чьей-то незакоммиченной
    работой. Страж обязан сказать и оставить решение человеку.
    """
    корень = _корень(tmp_path)
    копия = _копия(корень)
    (копия / "CLAUDE.md").write_text("СТАРЫЕ ПРАВИЛА\n", encoding="utf-8")

    вывод = _запустить(str(копия))

    assert not (копия / "CLAUDE.md").is_symlink(), "чужое расхождение затирать нельзя"
    assert (копия / "CLAUDE.md").read_text() == "СТАРЫЕ ПРАВИЛА\n"
    assert "РАЗОШЕДШАЯСЯ" in вывод


def test_битая_ссылка_восстановлена(tmp_path) -> None:
    корень = _корень(tmp_path)
    копия = _копия(корень)
    (копия / "CLAUDE.md").symlink_to("../../../НЕТУ.md")

    вывод = _запустить(str(копия))

    assert (копия / "CLAUDE.md").read_text() == "ПРАВИЛА ПРОЕКТА\n"
    assert "битой" in вывод


@pytest.mark.parametrize("ввод", ["не json", "", "null", '{"cwd": "/нет/такого"}', "[]"])
def test_мусор_на_входе_проходит_молча(ввод) -> None:
    """ПАРНЫЙ на пропуск: fail-open. Ошибка стража не смеет ронять старт сессии."""
    p = subprocess.run([str(HOOK)], input=ввод, capture_output=True, text=True, timeout=30)
    assert p.returncode == 0
    assert p.stdout == ""


def test_доставка_в_вершину_копии_а_не_в_подкаталог(tmp_path) -> None:
    """Сессия может начаться в подкаталоге рабочей копии.

    Класть файлы рядом с текущим каталогом нельзя: проверка перед отправкой ищет
    список приватных слов в ВЕРШИНЕ копии, и при доставке мимо неё она останется
    без списка — то есть слепой, — а доставщик отрапортует об успехе.
    """
    корень = _корень(tmp_path)
    копия = _копия(корень)
    глубоко = копия / "claude_memory" / "вложенно"
    глубоко.mkdir(parents=True)

    _запустить(str(глубоко))

    assert (копия / "CLAUDE.md").is_symlink(), "правила обязаны лечь в вершину копии"
    assert (копия / ".claude" / "private-words.txt").is_symlink(), (
        "список слов обязан лечь туда, где его ищет проверка перед отправкой")
    assert not (глубоко / "CLAUDE.md").exists(), "в подкаталоге файлам не место"


def test_ссылка_на_посторонний_файл_поправлена(tmp_path) -> None:
    """Живая ссылка, ведущая не на основной файл, принималась молча.

    Это возвращает ровно ту беду, ради которой страж написан: копия отстала, но
    теперь под видом ссылки — и выглядит исправной.
    """
    корень = _корень(tmp_path)
    копия = _копия(корень)
    посторонний = tmp_path / "чужие-правила.md"
    посторонний.write_text("СТАРЫЕ ЧУЖИЕ ПРАВИЛА\n", encoding="utf-8")
    (копия / "CLAUDE.md").symlink_to(посторонний)

    вывод = _запустить(str(копия))

    assert (копия / "CLAUDE.md").read_text() == "ПРАВИЛА ПРОЕКТА\n", (
        "ссылка вела не на основной файл — сессия шла бы по чужому тексту")
    assert вывод.strip(), "молчаливая подмена неотличима от исправного состояния"
