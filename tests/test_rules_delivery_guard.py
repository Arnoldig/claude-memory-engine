"""Доставка правил в рабочую ветку: режим выбирается ВОПРОСОМ К GIT, а не по проекту.

Половина случаев здесь — на ПРОПУСК, и это не вежливость к коду, а требование:
страж, который шумит без повода, отключают вместе с настоящими находками, а
страж, молчащий всегда, неотличим от исправного. Поэтому у каждого срабатывания
есть парный случай, где вывод обязан быть пустым.

ПОЧЕМУ РЕЖИМА ДВА. Способ доставки — свойство ФАЙЛА, а не проекта:

• файл ИГНОРИРУЕТСЯ git — в рабочую ветку он не приедет никогда, и подменить его
  ссылкой безопасно: ссылке некуда закоммититься. Так лежат `CLAUDE.md` и списки
  слов этого репозитория;
• файл ОТСЛЕЖИВАЕТСЯ git — его кладёт сама выкладка, а ссылка поверх него меняет
  тип файла (100644 → 120000). Замерено в одноразовом репозитории: `git status`
  показывает ` T`, первый же `git add -A` увозит ссылку в коммит, и в чистом клоне
  остаётся ссылка наружу репозитория — текста правил нет ни у кого.

Поэтому тесты строят НАСТОЯЩИЙ репозиторий и НАСТОЯЩУЮ рабочую ветку: в поддельной
папке `git ls-files` отвечает про другой путь, и проверка мерила бы не то.

Проверяется поведение, а не текст сообщений: сверяем факт (появилась ссылка,
файл остался нетронутым), а слова — лишь по опорному куску.
"""
import json
import os
import subprocess
from pathlib import Path

import pytest

HOOK = Path(__file__).resolve().parents[1] / ".claude" / "hooks" / "rules_delivery_guard.sh"

_GIT_ENV = {**os.environ, "GIT_AUTHOR_NAME": "t", "GIT_AUTHOR_EMAIL": "t@t",
            "GIT_COMMITTER_NAME": "t", "GIT_COMMITTER_EMAIL": "t@t"}


def _git(корень: Path, *аргументы: str) -> str:
    p = subprocess.run(["git", "-C", str(корень), *аргументы],
                       capture_output=True, text=True, env=_GIT_ENV, timeout=30)
    assert p.returncode == 0, f"git {' '.join(аргументы)}: {p.stderr}"
    return p.stdout


def _запустить(cwd: str) -> str:
    p = subprocess.run([str(HOOK)], input=json.dumps({"cwd": cwd}),
                       capture_output=True, text=True, timeout=30,
                       env={**os.environ, "RULES_DELIVERY_STRICT": "1"})
    assert p.returncode == 0, "SessionStart не имеет права ронять старт сессии"
    assert p.stderr == "", f"страж споткнулся молча: {p.stderr}"
    return p.stdout


def _корень(tmp_path: Path, слова: bool = True, отслеживать_правила: bool = False) -> Path:
    """Репозиторий с правилами. По умолчанию правила ИГНОРИРУЮТСЯ — как в этом проекте."""
    корень = tmp_path / "проект"
    (корень / ".claude").mkdir(parents=True)
    (корень / "CLAUDE.md").write_text("ПРАВИЛА ПРОЕКТА\n", encoding="utf-8")
    if слова:
        (корень / ".claude" / "private-words.txt").write_text("слово\n", encoding="utf-8")
    игнор = ".claude/private-words.txt\n"
    if not отслеживать_правила:
        игнор = "CLAUDE.md\n" + игнор
    (корень / ".gitignore").write_text(игнор, encoding="utf-8")
    (корень / "код.py").write_text("x = 1\n", encoding="utf-8")
    _git(корень, "init", "-q", "-b", "main", ".")
    _git(корень, "add", "-A")
    _git(корень, "commit", "-qm", "init")
    return корень


def _копия(корень: Path, имя: str = "wt") -> Path:
    """НАСТОЯЩАЯ рабочая ветка git там, где её ищет страж."""
    копия = корень / ".claude" / "worktrees" / имя
    _git(корень, "worktree", "add", "-q", str(копия), "-b", f"ветка-{имя}")
    return копия


# ── Файл ИГНОРИРУЕТСЯ git: доставляем ссылкой ───────────────────────────────

def test_доставляет_когда_правил_нет(tmp_path) -> None:
    корень = _корень(tmp_path)
    копия = _копия(корень)

    вывод = _запустить(str(копия))

    assert (копия / "CLAUDE.md").is_symlink(), "правила должны приехать ССЫЛКОЙ, не копией"
    assert (копия / "CLAUDE.md").read_text() == "ПРАВИЛА ПРОЕКТА\n"
    assert "правила проекта" in вывод, "молчаливая доставка неотличима от её отсутствия"


def test_молчит_когда_всё_на_месте(tmp_path) -> None:
    """ПАРНЫЙ на пропуск: второй старт в той же ветке обязан пройти беззвучно."""
    корень = _корень(tmp_path)
    копия = _копия(корень)
    _запустить(str(копия))

    assert _запустить(str(копия)) == "", "страж шумит на исправной ветке — его отключат"


def test_молчит_вне_рабочей_ветки(tmp_path) -> None:
    """ПАРНЫЙ на пропуск: в главной папке файл настоящий, чинить нечего."""
    корень = _корень(tmp_path)

    assert _запустить(str(корень)) == ""
    assert not (корень / "CLAUDE.md").is_symlink(), "основной файл трогать нельзя"


