def test_ссылка_на_посторонний_файл_поправлена(tmp_path) -> None:
    """Живая ссылка, ведущая не на основной файл, принималась молча.

    Это возвращает ровно ту беду, ради которой страж написан: копия отстала, но
    теперь под видом ссылки — и выглядит исправной.
    """
    корень = _корень(tmp_path)
    копия = _копия(корень)
    посторонний = tmp_path / "чужие-правила.md"
    посторонний.write_text("СТАРЫЕ ЧУЖИЕ ПРАВИЛА\n", encoding="utf-8")
    (копия / "CLAUDE.md").symlink_to(посторонний)

    вывод = _запустить(str(копия))

    assert (копия / "CLAUDE.md").read_text() == "ПРАВИЛА ПРОЕКТА\n", (
        "ссылка вела не на основной файл — сессия шла бы по чужому тексту")
    assert вывод.strip(), "молчаливая подмена неотличима от исправного состояния"


def test_доставка_в_вершину_ветки_а_не_в_подкаталог(tmp_path) -> None:
    """Сессия может начаться в подкаталоге рабочей ветки.

    Класть файлы рядом с текущим каталогом нельзя: проверка перед отправкой ищет
    список приватных слов в ВЕРШИНЕ ветки, и при доставке мимо неё она останется
    без списка — то есть слепой, — а доставщик отрапортует об успехе.
    """
    корень = _корень(tmp_path)
    копия = _копия(корень)
    глубоко = копия / "claude_memory" / "вложенно"
    глубоко.mkdir(parents=True)

    _запустить(str(глубоко))

    assert (копия / "CLAUDE.md").is_symlink(), "правила обязаны лечь в вершину ветки"
    assert (копия / ".claude" / "private-words.txt").is_symlink(), (
        "список слов обязан лечь туда, где его ищет проверка перед отправкой")
    assert not (глубоко / "CLAUDE.md").exists(), "в подкаталоге файлам не место"


