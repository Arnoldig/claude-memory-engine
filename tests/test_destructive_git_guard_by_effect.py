"""Страж разрушительных git-команд, проверенный ПО ПОСЛЕДСТВИЮ.

Отличие от `test_destructive_git_guard.py` — в том, откуда берётся ожидание.
Там перечень команд написан рукой, и ожидание («эта опасна, та нет») задаёт
автор теста. Значит забытая форма команды выпадает СРАЗУ ИЗ ОБОИХ: и из стража,
и из теста, а проверка мутацией этого не показывает — она подтверждает, что
написанный тест умеет краснеть, и молчит о том, чего не написали вовсе.

Здесь ожидание не задаётся, а ЗАМЕРЯЕТСЯ: команда по-настоящему выполняется в
одноразовом репозитории, и «опасна» означает «несохранённого стало меньше».
Автор теста не может забыть форму, потому что ничего не перечисляет по памяти:
формы порождаются перебором написаний, а вердикт выносит сам git.

Замерено 2026-07-20 этой проверкой на HEAD: `git clean --force` уносил
неотслеживаемый файл, а страж молчал — длинной формы не было в его шаблоне,
и потому её не было и в рукописном тесте. Ровно тот класс, который чинили
заявкой #24 («страж ловил по одной форме каждой команды»).
"""
import json
import subprocess
from pathlib import Path

import pytest

HOOK = Path(__file__).resolve().parents[1] / ".claude" / "hooks" / "destructive_git_guard.sh"

# Глаголы — из первоисточника (git help), а не из шаблонов стража: перечень,
# списанный со стража, никогда не найдёт пропущенный стражем глагол.
ГЛАГОЛЫ = ["checkout", "switch", "restore", "clean", "reset", "stash", "rm"]

# Написания одного и того же флага. Ось «как записано», на которой стража
# ловили уже трижды: `-F=файл` / `-Fфайл` (0.16.0), одна форма на команду (#24).
ФЛАГИ = ["", "-f", "--force", "-fd", "-df", "--force -d", "-f .", "--force .",
         "--hard", "--discard-changes", "-- .", "HEAD", "."]


def _песочница(корень: Path) -> Path:
    """Репозиторий, где есть что терять: правка в отслеживаемом + новый файл."""
    subprocess.run(["git", "init", "-q", str(корень)], check=True)
    (корень / "f.txt").write_text("сохранено\n")
    subprocess.run(["git", "-C", str(корень), "add", "-A"], check=True)
    subprocess.run(["git", "-C", str(корень), "-c", "user.email=t@t", "-c", "user.name=t",
                    "commit", "-qm", "init"], check=True)
    (корень / "f.txt").write_text("НЕСОХРАНЁННАЯ ПРАВКА\n")   # правка без коммита
    (корень / "новый.txt").write_text("НЕСОХРАНЁННЫЙ ФАЙЛ\n")  # неотслеживаемый
    return корень


def _несохранённое(репо: Path) -> set:
    """Что будет потеряно безвозвратно, если сейчас снести рабочее дерево.

    Пара (путь, содержимое) для всего, чего нет в последнем коммите. Меряем
    содержимое, а не вывод `git status`: `git add` меняет статус, ничего не
    теряя, и по статусу это выглядело бы как потеря.
    """
    из_коммита = subprocess.run(["git", "-C", str(репо), "show", "HEAD:f.txt"],
                                capture_output=True, text=True).stdout
    живое = set()
    for путь in репо.rglob("*"):
        if путь.is_file() and ".git" not in путь.parts:
            текст = путь.read_text(errors="replace")
            if not (путь.name == "f.txt" and текст == из_коммита):
                живое.add((путь.relative_to(репо).as_posix(), текст))
    return живое


def _страж_блокирует(cmd: str, cwd: str) -> bool:
    """True — отклонено кодом 2 с объяснением. Иное поведение → провал теста.

    Код 1 и молчание разбираются отдельно: для PreToolUse это ПРОПУСК, то есть
    мёртвый страж выглядит точно как разрешающий.
    """
    p = subprocess.run(
        [str(HOOK)],
        input=json.dumps({"tool_name": "Bash", "cwd": cwd, "tool_input": {"command": cmd}}),
        capture_output=True, text=True, timeout=30,
    )
    if p.returncode == 2 and "Заблокировано" in p.stderr:
        return True
    if p.returncode == 0 and not p.stderr.strip():
        return False
    pytest.fail(f"страж сломан: код={p.returncode}, stderr={p.stderr[:300]!r}")


КОМАНДЫ = sorted({f"git {г} {ф}".strip() for г in ГЛАГОЛЫ for ф in ФЛАГИ})


@pytest.mark.parametrize("cmd", КОМАНДЫ)
def test_что_уносит_работу_то_страж_и_блокирует(cmd, tmp_path) -> None:
    """Единственное правило: команда унесла несохранённое → страж обязан был её
    остановить. Ожидание выводится из последствия, поэтому забыть форму нельзя.
    """
    проба = _песочница(tmp_path / "проба")
    было = _несохранённое(проба)
    subprocess.run(cmd, shell=True, cwd=str(проба), capture_output=True, timeout=30)
    стало = _несохранённое(проба)

    if стало >= было:
        return  # ничего не потеряно (или команда не выполнилась) — вопроса нет

    контроль = _песочница(tmp_path / "контроль")
    потеряно = sorted(п for п, _ in было - стало)
    assert _страж_блокирует(cmd, str(контроль)), (
        f"{cmd!r} безвозвратно уносит несохранённое {потеряно}, а страж молчит"
    )
