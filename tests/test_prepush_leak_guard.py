"""Проверка перед отправкой: она сторожит то, чего не видит страж команд.

Страж команд закрывает свой канал — отправку текста вызовом `gh`. Но приватное
слово уезжает и вторым путём: в составе коммита. Это ЕДИНСТВЕННЫЙ слой, который
утечку предотвращает; всё серверное только обнаруживает её, когда коммиты уже
лежат на публичном сервере.

Замерено 2026-07-20 — три дефекта, каждому здесь соответствует падающий тест:
  • посторонний выход по отсутствию файла пакета стоял ВЫШЕ проверки слов и гасил
    её целиком по причине, к утечке отношения не имеющей;
  • проверялось рабочее дерево, а не уходящие коммиты: слово, живущее только в
    истории, проходило незамеченным;
  • список слов, который не удалось прочитать, давал молчаливый пропуск.

Существовавший тест `test_prepush_blocks_and_passes` эти дефекты не ловил: он сам
создавал файл пакета и вызывал хук без входных данных, то есть был зелёным ровно
на той настройке, где ни один из дефектов не проявляется.
"""
import subprocess
from pathlib import Path

PREPUSH = Path(__file__).resolve().parents[1] / ".githooks" / "pre-push"
СЛОВО = "ВыдуманноеПриватноеСлово"
НУЛИ = "0" * 40


def _git(репо: Path, *args) -> str:
    p = subprocess.run(["git", "-C", str(репо)] + list(args),
                       capture_output=True, text=True)
    return p.stdout.strip()


def _репозиторий(корень: Path, со_списком: bool = True) -> Path:
    """Репозиторий с origin, куда уже отправлен один коммит."""
    удалённый = корень / "origin.git"
    subprocess.run(["git", "init", "-q", "--bare", str(удалённый)], check=True)
    репо = корень / "repo"
    репо.mkdir()
    subprocess.run(["git", "init", "-q", str(репо)], check=True)
    for a in (["config", "user.email", "t@t"], ["config", "user.name", "t"],
              ["remote", "add", "origin", str(удалённый)]):
        _git(репо, *a)
    (репо / ".claude").mkdir()
    if со_списком:
        (репо / ".claude" / "private-words.txt").write_text(СЛОВО + "\n", encoding="utf-8")
    (репо / "readme.md").write_text("чисто\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "первый")
    _git(репо, "push", "-q", "--no-verify", "-u", "origin", "HEAD:refs/heads/main")
    return репо


def _отправка(репо: Path) -> subprocess.CompletedProcess:
    """Зовём хук так, как это делает git: список ссылок приходит на стандартный ввод."""
    локальный = _git(репо, "rev-parse", "HEAD")
    удалённый = _git(репо, "rev-parse", "origin/main") or НУЛИ
    ввод = f"refs/heads/main {локальный} refs/heads/main {удалённый}\n"
    return subprocess.run(["bash", str(PREPUSH), "origin", str(репо.parent / "origin.git")],
                          input=ввод, cwd=str(репо), capture_output=True, text=True)


def _блокирует(p: subprocess.CompletedProcess) -> bool:
    if p.returncode != 0:
        assert p.stderr.strip(), "блокировка без объяснения — человек не поймёт, что делать"
        return True
    return False


def test_слово_только_в_истории_ловится(tmp_path) -> None:
    """ГЛАВНЫЙ случай. Слово внесли и убрали — в файлах его нет, а в уходящих
    коммитах есть. Проверка рабочего дерева такое пропускает целиком."""
    репо = _репозиторий(tmp_path)
    (репо / "заметка.md").write_text(f"черновик про {СЛОВО}\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "черновик")
    (репо / "заметка.md").write_text("черновик обезличен\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "обезличено")

    assert not (репо / "заметка.md").read_text().count(СЛОВО), "в дереве слова уже нет"
    assert _блокирует(_отправка(репо)), "слово уезжает в истории, а проверка его не видит"


def test_слово_в_сообщении_коммита_ловится(tmp_path) -> None:
    репо = _репозиторий(tmp_path)
    (репо / "файл.md").write_text("текст\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", f"правка для {СЛОВО}")

    assert _блокирует(_отправка(репо))


def test_отсутствие_файла_пакета_не_отключает_проверку(tmp_path) -> None:
    """Выход по отсутствию файла пакета заведён ради напоминания о выпуске.
    Гасить им защиту от утечки нельзя: причины не связаны никак."""
    репо = _репозиторий(tmp_path)
    (репо / "заметка.md").write_text(f"тут {СЛОВО}\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "правка")

    assert not (репо / "claude_memory").exists(), "файла пакета в этом репозитории нет"
    assert _блокирует(_отправка(репо)), "посторонняя причина отключила защиту от утечки"


def test_нечитаемый_список_слов_блокирует(tmp_path) -> None:
    """Список есть, но прочитать не удалось — значит неизвестно, что запрещено.
    Молчание здесь неотличимо от «запретного не найдено»."""
    репо = _репозиторий(tmp_path)
    (репо / ".claude" / "private-words.txt").chmod(0o000)
    (репо / "файл.md").write_text("любой текст\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "правка")
    try:
        assert _блокирует(_отправка(репо))
    finally:
        (репо / ".claude" / "private-words.txt").chmod(0o644)


# ── ПАРНЫЕ случаи на ПРОПУСК ────────────────────────────────────────────────

def test_чистая_отправка_проходит(tmp_path) -> None:
    """Без этого случая набор зеленел бы и на хуке, блокирующем всё подряд."""
    репо = _репозиторий(tmp_path)
    (репо / "файл.md").write_text("совершенно обычный текст\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "обычная правка")

    assert not _блокирует(_отправка(репо))


def test_без_списка_слов_проходит(tmp_path) -> None:
    """У стороннего участника списка нет вовсе. Хук, ломающий чужую отправку,
    снимут вместе со всей защитой — здесь пропуск намеренный."""
    репо = _репозиторий(tmp_path, со_списком=False)
    (репо / "файл.md").write_text(f"даже со словом {СЛОВО}\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "правка")

    assert not _блокирует(_отправка(репо))


def test_слово_уже_на_сервере_не_мешает(tmp_path) -> None:
    """Проверяем то, что УХОДИТ. Уже отправленное трогать поздно и незачем:
    иначе каждая следующая отправка блокировалась бы вечно."""
    репо = _репозиторий(tmp_path)
    (репо / "старое.md").write_text(f"давно тут {СЛОВО}\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "старое")
    _git(репо, "push", "-q", "--no-verify", "origin", "HEAD:refs/heads/main")

    (репо / "новое.md").write_text("чистая правка\n", encoding="utf-8")
    _git(репо, "add", "-A")
    _git(репо, "commit", "-qm", "новое")

    assert not _блокирует(_отправка(репо))


def test_удаление_ветки_проходит(tmp_path) -> None:
    """Удаление ветки: локальная ссылка в нулях, содержимого не уходит вовсе."""
    репо = _репозиторий(tmp_path)
    ввод = f"(delete) {НУЛИ} refs/heads/main {_git(репо, 'rev-parse', 'HEAD')}\n"
    p = subprocess.run(["bash", str(PREPUSH), "origin", str(tmp_path / "origin.git")],
                       input=ввод, cwd=str(репо), capture_output=True, text=True)
    assert p.returncode == 0
