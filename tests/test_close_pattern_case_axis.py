"""Третья координата эталона форм закрытия — РЕГИСТР слова (заявка #20).

GitHub документирует регистр наравне с двоеточием: «The keywords can be followed
by colons or in uppercase», примеры `CLOSES #10`, `CLOSES: #10`, и строчное
`resolves #123` в его же примере на несколько задач.
Сверено 2026-07-20 по docs.github.com, раздел про связывание запроса с задачей.

Зачем ось. Лаг-страж считает шаблон потребителя «законной полной заменой», когда
промахнулись ВСЕ зонды. Пока зонды идут в одном регистре, копия дефолта,
потерявшая нечувствительность к регистру, промахивается на всех — и страж
молчит о шаблоне, который узнаёт треть документированных форм. Ось регистра
разводит эти два случая: часть зондов проходит, часть нет — значит отстал.

Перечень зондов здесь не переписан, а взят у самого модуля: сочетания — это
произведение объявленных им осей. Ось, добавленная позже, попадёт в проверку
сама.
"""
import itertools

import pytest

from claude_memory import self_check as SC
from claude_memory import stop_check as S


def _зонды():
    return [
        (регистр(слово), шаблон.format(word=регистр(слово), id=S.GITHUB_CLOSE_PROBE_ID))
        for слово, (_суффикс, шаблон), (_имя, регистр)
        in itertools.product(S.GITHUB_CLOSE_KEYWORDS, S.GITHUB_CLOSE_SYNTAXES,
                             S.GITHUB_CLOSE_CASES)
    ]


def test_эталон_объявляет_ось_регистра() -> None:
    """Ось обязана быть объявлена модулем, а не подразумеваться в голове автора."""
    assert hasattr(S, "GITHUB_CLOSE_CASES"), "у эталона нет оси РЕГИСТРА"
    assert len(S.GITHUB_CLOSE_CASES) >= 3, "документированы Заглавная, ВЕРХНИЙ и строчный"


def test_ожидание_не_зашито_литералом() -> None:
    """Идентификатор зонда задан в ОДНОМ месте и оттуда же берётся ожидание.

    Литерал `!= "42"` в сверке — вторая замороженная величина рядом с порогом:
    при смене вида идентификатора в шаблонах она молча начинает мерить не то,
    и страж жалуется на верное, маскируя настоящую дыру.
    """
    assert hasattr(S, "GITHUB_CLOSE_PROBE_ID"), "идентификатор зонда не назван"
    for _суффикс, шаблон in S.GITHUB_CLOSE_SYNTAXES:
        assert "{id}" in шаблон, f"шаблон {шаблон!r} держит идентификатор литералом"


def test_дефолт_узнаёт_все_сочетания() -> None:
    """Дефолт библиотеки обязан покрывать весь документированный перечень."""
    from claude_memory.config import MemoryConfig

    дефолт = MemoryConfig.task_close_pattern
    промахи = [текст for _слово, текст in _зонды()
               if S.extract_closed_task(f"feat: {текст}", дефолт) != S.GITHUB_CLOSE_PROBE_ID]
    assert not промахи, f"дефолт не узнаёт документированные формы: {промахи[:5]}"


@pytest.mark.parametrize("порча,описание", [
    (lambda p: p.replace("(?i)", ""), "копия потеряла нечувствительность к регистру"),
])
def test_отставшая_по_регистру_копия_названа(cfg, порча, описание) -> None:
    """Шаблон, узнающий лишь часть регистров, обязан вызывать жалобу, а не молчание.

    До появления оси регистра этот случай выглядел как «полная замена»: все зонды
    шли с Заглавной, все промахивались, и страж считал шаблон законно своим.
    """
    from dataclasses import replace

    отставший = порча(cfg.task_close_pattern)
    assert отставший != cfg.task_close_pattern, "порча ничего не изменила — тест бессилен"
    испорченный = replace(cfg, task_close_pattern=отставший)

    жалобы = SC.close_pattern_lag_issues(испорченный)
    assert жалобы, f"{описание}: страж молчит"
