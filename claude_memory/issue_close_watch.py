"""Второй источник сигнала «задачу закрыли»: команда `gh issue close`, а не только коммит.

ЗАЧЕМ. `stop_check.closure_reminder` узнаёт о закрытии задачи ЕДИНСТВЕННЫМ способом —
читает текст последнего коммита (`git log -1 --format=%B`). Пока задачи вели в markdown-
трекере, закрытие И БЫЛО коммитом, правившим файл трекера, и это совпадало. После
перехода на GitHub Issues закрывают командой `gh issue close`, которая коммита не
создаёт вообще: страж честно не находит совпадения и молчит, а «не нашёл» снаружи
неотличимо от «нечего было находить». Замер на двух живых проектах (июль 2026): там,
где проект не завёл собственной компенсации, из 10 закрытых задач страж увидел 2.
Это тот же общий знаменатель, что у всех прецедентов движка: разбор молча возвращает
пустоту. Лечится не расширением ШАБЛОНА (он верен), а расширением ИСТОЧНИКА.

КАК. PostToolUse на Bash замечает совершённое закрытие и кладёт метку в память; Stop
эту метку читает и, если урока про задачу нет, блокирует завершение — ровно тем же
текстом-требованием, что и коммит-путь. Коммит-путь при этом не трогается НИ БАЙТОМ:
новый источник умеет только ДОБАВИТЬ срабатывание, снять существующее он не может.

ПОЧЕМУ PostToolUse, А НЕ PreToolUse. Нужен ФАКТ закрытия, а не намерение. До выполнения
команду может отклонить чужой страж (у обоих живых потребителей на `gh issue close`
висит свой PreToolUse-привратник основания), может отказать человек в запросе
разрешения, может упасть сам `gh`. Метка по намерению — это ложный долг и ложная
блокировка завершения. Движок закрытие НЕ запрещает: запрет — политика проекта, дело
движка — запомнить и спросить про урок.

ПОЧЕМУ МЕТКА В ПАМЯТИ, А НЕ В TMPDIR. Все прочие метки движка живут в `tempfile.
gettempdir()`, и у них есть известная грабля: TMPDIR хук-процессов меняется при
перезапуске приложения, метки теряются. Здесь потеря метки = тихий пропуск закрытия,
то есть РЕЦИДИВ ровно того дефекта, который этот модуль чинит. Поэтому метка лежит в
`memory_dir` (он вне git, общий для всех рабочих деревьев проекта, переживает
перезапуск) под приватным префиксом — значит невидим для указателя и ретривера.
Но `memory_dir` общий И для ПАРАЛЛЕЛЬНЫХ сессий в разных деревьях, а закрытие,
сделанное в одном дереве, не должно блокировать завершение в другом (у той сессии нет
ни контекста, ни урока). Поэтому в имя файла входит отпечаток рабочего каталога, и
Stop смотрит только свои метки. Отпечаток — sha256, НЕ встроенный `hash()`: он
рандомизируется per-process через PYTHONHASHSEED, а каждый хук — отдельный процесс.

ЧЕГО ЭТОТ СТРАЖ НЕ ВИДИТ (названо вслух, чинить не планируется). Разбор команды
оболочки здесь — эвристика, а не грамматика shell; полноценный парсер внутри хука дал
бы дефекты хуже закрытых.
  • Ложный ПРОПУСК: alias'ы и обёртки-скрипты вокруг `gh`, `xargs gh …`, закрытие
    через `gh api`/curl, закрытие в вебе или в другом терминале мимо Claude Code.
    Всё это остаётся на попечении коммит-пути.
  • Ложный ДЕТЕКТ: вложенные кавычки и содержимое heredoc, где фраза закрытия лишь
    УПОМИНАЕТСЯ (тестовая обвязка, документация). Цена мала и самогасится: лишний
    вопрос про урок, снимаемый любым уроком или сроком годности метки.
Обе зеркальные грабли самого детекта закрыты (см. `detect_close`) и закреплены тестами.
"""
from __future__ import annotations

import hashlib
import json
import os
import re
from pathlib import Path
from typing import Optional

from . import stop_check
from .config import MemoryConfig
from .messages import msg

# ── Детектор команды ────────────────────────────────────────────────────────

# Командная позиция: начало строки или разделитель, затем необязательные обёртки
# (VAR=val, sudo, env, …) и необязательный путь к бинарю (/opt/homebrew/bin/gh).
_PRE = r"(?:[A-Za-z_][A-Za-z0-9_]*=\S+\s+|(?:sudo|env|command|nohup|time|setsid|doas)\s+)*"
_GH = r"(?:[\w./-]*/)?gh"
_SEP = r"(?:^|[;&|(){}`])\s*"
_CLOSE_RE = re.compile(_SEP + _PRE + _GH + r"\s+issue\s+close\b", re.M)
# Номер задачи в хвосте аргументов: голое число либо хвост URL вида …/issues/123.
# Границы — через lookaround по «словному» символу, а НЕ через требование пробела:
# пробелом номер окружён не всегда, и требование пробела теряло `(gh issue close 11)`
# (скобка справа) и `gh issue close "5"` (кавычка слева). Потерянный номер — это метка
# «закрытие без номера», которую нечем погасить точным поиском урока, то есть лишний
# вопрос на ровном месте. Кавычки вокруг числа допускаются явно.
# Цифры ЯВНО ascii (`[0-9]`, не `\d`): `\d` в Python по умолчанию юникодный и принимает,
# например, полноширинную `５` — номера задач такими не бывают, а разобранный номер идёт
# в ИМЯ ФАЙЛА метки. Потолок длины — по той же причине: 500-значный «номер» даёт имя
# длиннее лимита файловой системы, запись падает, и закрытие просто не запоминается.
# Ни то, ни другое не даёт выйти из каталога меток (`/` и `.` сюда не попадают ни при
# каком вводе), но сужать то, что подставляется в путь, дешевле, чем доказывать
# безопасность широкого класса.
_NUM_RE = re.compile(r"""(?<![\w-])(?:\S*/issues/)?["']?([0-9]{1,12})["']?(?![\w-])""")
# Граница хвоста аргументов: разделитель команд или начало подстановки.
_TAIL_SPLIT_RE = re.compile(r"[;&|\n]|\$\(|`")


def mask_quotes(s: str) -> str:
    """Содержимое кавычек → заполнитель ТОЙ ЖЕ длины.

    Длина принципиальна: детект и вырезание хвоста работают по смещениям в ОДНОЙ строке,
    и заполнитель другой длины сдвинул бы хвост — он отрезался бы не там."""
    def repl(m: "re.Match[str]") -> str:
        body = m.group(0)
        return body[0] + "\x00" * (len(body) - 2) + body[-1]

    s = re.sub(r'"[^"]*"', repl, s)
    return re.sub(r"'[^']*'", repl, s)


def detect_close(command: str) -> Optional[str]:
    """Номер закрываемой задачи, `""` если закрытие есть, а номер не разобран, None — если
    закрытия в команде нет.

    Пустая строка и None различимы намеренно: `gh issue close "$N"` — это СОСТОЯВШЕЕСЯ
    закрытие с неизвестным номером, и молчать о нём нельзя; трактовать его как «закрытия
    не было» значило бы вернуть ровно тот тихий пропуск, против которого модуль заведён.

    Две зеркальные ошибки такого разбора, обе воспроизведены на живых командах:
      • ложный ДЕТЕКТ, если не маскировать кавычки: разделитель ВНУТРИ строкового
        аргумента принимается за границу команды, и `grep "foo; gh issue close" f`
        засчитывается за закрытие;
      • ложное МОЛЧАНИЕ, если признак искать по всей команде, а не в хвосте аргументов
        самого `close`: чужой флаг из соседней команды сходит за свой.
    Здесь стоят обе защиты. Детект идёт по КОМАНДНОЙ ПОЗИЦИИ (начало строки или
    разделитель), а не поиском подстроки: иначе `echo gh issue close` ловится зря."""
    if not command:
        return None
    masked = mask_quotes(command)
    m = _CLOSE_RE.search(masked)
    if not m:
        return None
    # Хвост аргументов САМОГО close — до ближайшего разделителя или подстановки. Номер
    # ищем в исходной (немаскированной) строке по тем же смещениям: маска сохраняет
    # длину, поэтому `gh issue close "5"` отдаёт номер, а не теряет его под заполнителем.
    end = m.end()
    cut = _TAIL_SPLIT_RE.search(masked, end)
    tail = command[end:cut.start() if cut else len(command)]
    # Дальше первого флага не смотрим: `gh issue close` кладёт номер ПЕРВЫМ аргументом, а
    # за флагами начинается человеческий текст. Без этой границы `gh issue close $N
    # --comment "попытка 3"` отдавал номер «3» из чужой фразы — а ложный номер хуже
    # неизвестного: он гасит долг по уроку про ДРУГУЮ задачу, то есть возвращает тихий
    # пропуск. Цена границы — редкая форма `close --repo o/r 5` даст «номер не разобран»,
    # и урок всё равно будет затребован, просто без номера в тексте.
    flag = re.search(r"\s-{1,2}\w", tail)
    if flag:
        tail = tail[:flag.start()]
    # Потолок длины хвоста — защита от квадратичного разбора, а не микрооптимизация.
    # `(?:\S*/issues/)?` на длинном СПЛОШНОМ токене со слэшами и без цифр перебирает
    # слэши с каждой стартовой позиции: замерено 40 КБ → 3.9 с, 60 КБ уже за таймаутом
    # хука (10 с). Убитый по таймауту хук теряет НАСТОЯЩЕЕ закрытие молча — рецидив
    # ровно того дефекта, который чинит этот модуль. Номер задачи в реальных командах
    # живёт в первых десятках символов, так что граница не отсекает ничего живого.
    num = _NUM_RE.search(tail[:512])
    return num.group(1) if num else ""


def _tool_failed(event: dict) -> bool:
    """Явно ли команда не удалась. Fail-open: неизвестный формат ответа = считаем удачной.

    Осознанно НЕ смотрим на непустой stderr: `gh` пишет туда и при успехе. Ошибочно
    признать провал = потерять закрытие молча, а это дефект, который модуль и чинит;
    ошибочно признать успех = лишняя метка, гаснущая по сроку годности."""
    resp = event.get("tool_response")
    if not isinstance(resp, dict):
        return False
    return bool(resp.get("interrupted")) or bool(resp.get("is_error")) or resp.get("success") is False


# ── Метка «закрытие без урока» ──────────────────────────────────────────────

def marker_dir(cfg: MemoryConfig) -> Path:
    """Каталог меток внутри memory_dir. Приватный префикс → вне указателя и ретривера."""
    return Path(cfg.memory_dir) / f"{cfg.private_file_prefix}pending_close"


def _cwd_digest(cwd: str) -> str:
    """Отпечаток рабочего каталога — sha256, НЕ hash() (тот рандомизируется per-process,
    а каждый хук — отдельный процесс, и метка перестала бы находиться)."""
    try:
        real = os.path.realpath(cwd or ".")
    except OSError:
        real = cwd or "."
    return hashlib.sha256(real.encode("utf-8", "replace")).hexdigest()[:16]


def marker_path(cfg: MemoryConfig, cwd: str, issue: str) -> Path:
    """Путь метки. Номер санируется ПОВТОРНО, хотя `_NUM_RE` уже отдаёт только цифры:
    это единственное место, где разобранный из чужой команды текст попадает в путь
    файловой системы, и оно обязано оставаться безопасным даже если завтра шаблон
    разбора расширят. Ремень поверх подтяжек здесь дешевле разбора последствий."""
    safe = issue if issue.isdigit() and issue.isascii() and len(issue) <= 12 else ""
    return marker_dir(cfg) / f"{_cwd_digest(cwd)}_{safe or 'unknown'}.json"


def _unlink(path: Path) -> None:
    try:
        path.unlink()
    except OSError:
        pass


def _write_marker(path: Path, issue: str, cwd: str, now_ts: float, session_id: str) -> bool:
    """Атомарная запись метки: сначала временный файл, потом `os.replace`.

    Прямой `write_text` — это truncate + write, и параллельная сессия успевает прочитать
    метку ПУСТОЙ в окне между ними, счесть её битой и подмести. Свежее закрытие пропало
    бы молча — рецидив чинимого класса. Окно микросекундное, но `memory_dir` общий для
    всех рабочих деревьев, а параллельные сессии здесь норма, не исключение.
    Имя временного файла уникально по pid: две сессии не отберут его друг у друга."""
    tmp = path.with_name(f"{path.name}.{os.getpid()}.tmp")
    try:
        path.parent.mkdir(parents=True, exist_ok=True)
        tmp.write_text(json.dumps({
            "issue": issue or None,
            "ts": int(now_ts),
            "cwd": cwd,
            "session_id": session_id,
        }, ensure_ascii=False), encoding="utf-8")
        os.replace(str(tmp), str(path))
        return True
    except OSError:
        _unlink(tmp)  # не оставляем мусор, если упали на полпути
        return False  # память недоступна → не мешаем работе, закрытие просто не запомнено


def record_close(
    event: dict, cfg: MemoryConfig, cwd: str, now_ts: float, session_id: str = ""
) -> Optional[str]:
    """PostToolUse(Bash): заметить `gh issue close` и поставить метку. Текст-подтверждение
    в контекст или None.

    Подтверждение здесь — не вежливость, а сама диагностика: его ОТСУТСТВИЕ в момент
    закрытия и есть наблюдаемый признак «страж включён, а сигнал не дошёл». Молчащий
    страж неотличим от отсутствующего — против этого и заведён весь модуль.

    Быстрый выход без единого обращения к диску, если закрытия в команде нет: хук висит
    на КАЖДОЙ команде Bash, и путь без совпадения обязан быть бесплатным."""
    if not getattr(cfg, "task_close_lesson_gate", False):
        return None
    if not getattr(cfg, "task_close_command_watch", False):
        return None
    if str(event.get("tool_name") or "") != "Bash":
        return None
    command = str((event.get("tool_input") or {}).get("command") or "")
    issue = detect_close(command)
    if issue is None:
        return None
    if _tool_failed(event):
        return None
    # Урок про задачу уже записан — долга нет, метку не заводим. Это покрывает штатный
    # порядок «записал урок → выложил → закрыл задачу».
    if issue and stop_check.task_lesson_recorded(cfg, issue):
        return None
    if not _write_marker(marker_path(cfg, cwd, issue), issue, cwd, now_ts, session_id):
        return None
    key = "issue_close.ack" if issue else "issue_close.ack_unknown"
    return msg(cfg, key, task_id=issue)


def pending_reminder(cfg: MemoryConfig, cwd: str, now_ts: float) -> Optional[str]:
    """Stop: текст блокировки, если в ЭТОМ рабочем каталоге закрыли задачу и урока про неё
    нет. Иначе None. Заодно подметает протухшие и битые метки — в том числе чужие.

    Метка гаснет тремя способами, и это намеренная избыточность против вечной блокировки:
      1. урок с `#<номер>` найден (`task_lesson_recorded`, тот же поиск, что у коммит-пути);
      2. ЛЮБОЙ урок записан позже метки — так снимается долг, когда урок назван слагом,
         а не номером (оба живых проекта закрывают задачи слагом). Проверка намеренно
         слабее точной: та же логика, что у `should_remind` (коммит новее урока), и цена
         ошибки здесь — не спросить лишний раз, а не заблокировать навсегда;
      3. истёк срок годности метки (`task_close_marker_ttl_seconds`).
    Плюс fail-open диспетчера поверх всего. Страж, который может запереть сессию
    насмерть, хуже отсутствующего: его первым делом выключают целиком."""
    if not getattr(cfg, "task_close_lesson_gate", False):
        return None
    if not getattr(cfg, "task_close_command_watch", False):
        return None
    directory = marker_dir(cfg)
    try:
        entries = sorted(directory.glob("*.json"))
    except OSError:
        return None
    if not entries:
        return None
    ttl = int(getattr(cfg, "task_close_marker_ttl_seconds", 0) or 0)
    mine = _cwd_digest(cwd) + "_"
    newest_lesson = stop_check.newest_lesson_mtime(cfg)
    reason: Optional[str] = None
    for path in entries:
        try:
            data = json.loads(path.read_text(encoding="utf-8"))
            ts = int(data["ts"])
        except (OSError, ValueError, TypeError, KeyError):
            _unlink(path)          # битая метка не имеет права держать сессию
            continue
        if ttl and (now_ts - ts) > ttl:
            _unlink(path)          # подметаем и чужие: их некому больше убрать
            continue
        if not path.name.startswith(mine):
            continue               # закрытие из другого рабочего дерева — не наш разговор
        issue = str(data.get("issue") or "")
        if issue and stop_check.task_lesson_recorded(cfg, issue):
            _unlink(path)
            continue
        if newest_lesson > ts:
            _unlink(path)
            continue
        if reason is None:         # блокируем по первой, но цикл дохаживаем ради уборки
            key = ("stop_check.closure_command_reminder" if issue
                   else "stop_check.closure_command_reminder_unknown")
            reason = msg(cfg, key, task_id=issue)
    return reason
