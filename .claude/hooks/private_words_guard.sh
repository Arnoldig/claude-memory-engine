#!/bin/bash
# PreToolUse (matcher: Bash): БЛОКИРУЕТ публикацию текста в GitHub, если в нём есть слово
# из приватного списка — названия рабочих проектов, внутренние слаги, персональные данные.
#
# ЗАЧЕМ. Репозиторий публичный. Файлы и код перед пушем видит человек и ревью, а вот текст
# комментария к заявке уезжает в один вызов и уже не отзывается: правка комментария видна
# в истории правок GitHub. Именно так названия двух рабочих проектов попали в комментарии
# заявок #6 и #13 (вычищено вручную 18.07.2026). Git-хук их не поймал бы никогда:
# комментарии заявок в git не попадают вовсе.
#
# ЧТО СТЕРЕЖЁТ — ТОЛЬКО КАНАЛЫ ПУБЛИКАЦИИ, а не любое употребление слова. Разница
# принципиальная: страж, блокирующий `grep <слово>`, сделал бы невозможным сам аудит на эти
# слова, и его сняли бы в первый же день. Перечень узкий и закрытый: создание и правка
# заявок, комментариев, PR, релизов, gist, а также `gh api` методами записи.
# `git push` сюда НЕ входит: сообщения коммитов стережёт `.githooks/pre-push`, у него для
# этого есть доступ к самим коммитам, которого у разбора командной строки нет.
#
# СПИСОК СЛОВ ЖИВЁТ ВНЕ GIT — `.claude/private-words.txt` (в `.gitignore`). Класть его в
# публичный репозиторий нельзя: файл, перечисляющий то, что нужно скрыть, раскрывает это
# сам. Нет файла или он пуст → страж молчит: у внешнего участника проекта этих слов нет,
# и мешать ему нечем.
#
# ГРАНИЦЫ, НАЗВАННЫЕ ВСЛУХ (чинить не планируется):
#   • Текст, переданный через `--body-file <файл>`, проверяется чтением файла; текст из
#     подстановки (`--body "$(cat …)"`) — нет, содержимое подстановки на момент проверки
#     не существует. Это принятая дыра: закрыть её можно только исполнением команды.
#   • Слово ищется подстрокой и без учёта регистра: корень поймает и производные от него
#     (`имя` → `имя_001`). Ложные
#     срабатывания на общеупотребительных словах возможны, поэтому список ведётся вручную
#     и намеренно короткий.
#   • Кроме списка слов есть ШАБЛОНЫ (ключи, почта, телефон) — они в коде, потому что
#     формат токена публичен, скрывать в нём нечего, а версионировать полезно.
#     Но это НЕ полноценный детектор секретов: он знает ровно те формы, что
#     перечислены. Главный слой против ключей — push protection GitHub.
#   • Телефон ловится только в международной записи со знаком «плюс»: шаблон пошире
#     принимал бы версии и даты, а ложно блокирующего стража снимают целиком.
#
# FAIL-OPEN И ЕГО ЕДИНСТВЕННОЕ ИСКЛЮЧЕНИЕ. По ошибкам РАЗБОРА страж молчит и пропускает:
# чужой JSON, не тот инструмент, отсутствующий список слов — во всех этих случаях страж
# либо не понял вход, либо ему нечего защищать. Но ПОСЛЕ того как команда опознана как
# публикация, действует обратное правило: не смогли проверить → БЛОКИРУЕМ. Три таких
# исхода — сторожевой таймер, файл тела не обычного типа, файл тела сверх предела.
# Обоснование одно: не уложились/не прочли значит НЕ ЗНАЕМ, есть ли запретное, а
# необратимая публикация дороже одного лишнего разбора.
# ЗАМЕРЕНО: превышение таймаута хука PreToolUse ПРОПУСКАЕТ вызов, а не блокирует
# (контрольный опыт с кодом 2 блокировал, опыт со сном дольше таймаута — нет).
# Документация Claude Code об этом исходе не говорит. Отсюда следствие: замедлить стража
# = обойти стража, поэтому цена разбора здесь вопрос корректности, а не скорости.
set -uo pipefail

input=$(cat)
WORDS_FILE="${CLAUDE_PROJECT_DIR:-$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)}/.claude/private-words.txt"

HOOK_INPUT="$input" WORDS_FILE="$WORDS_FILE" python3 - <<'PY'
import json, os, re, signal, stat, sys

try:
    data = json.loads(os.environ.get("HOOK_INPUT", "{}"))
except Exception:
    sys.exit(0)                              # fail-open: чужой ввод не повод падать

if (data.get("tool_name") or "") != "Bash":
    sys.exit(0)
cmd = (data.get("tool_input") or {}).get("command", "") or ""
if not cmd:
    sys.exit(0)

# Быстрый выход без чтения диска: команда вообще не про публикацию.
if "gh" not in cmd:
    sys.exit(0)

try:
    # `errors="replace"` ОБЯЗАТЕЛЕН, а перехват широкий. Список правят руками, и одна
    # вставка из cp1251 роняла процесс UnicodeDecodeError ещё ДО шаблонов — то есть
    # один байт отключал и то, что от списка вообще не зависит. Замер: код 1 вместо 2,
    # токен уходил незаблокированным. Код 1 блокировкой НЕ является: у PreToolUse
    # блокирует только код 2, поэтому «страж упал» и «страж разрешил» неразличимы.
    # Порча файла со словами не должна отключать проверки, которым этот файл не нужен.
    with open(os.environ["WORDS_FILE"], encoding="utf-8", errors="replace") as fh:
        words = [w.strip() for w in fh if w.strip() and not w.lstrip().startswith("#")]
except Exception:                            # noqa: BLE001 — см. выше: OSError мало
    words = []                               # списка нет — но ШАБЛОНЫ ниже работают всегда:
                                             # ключ не должен уехать в публичный комментарий
                                             # и у того, кто списка слов не заводил.

# Каналы публикации. Узко и перечислимо: только то, что ОТПРАВЛЯЕТ текст наружу.
PUBLISH = re.compile(
    r"(?:^|[;&|(){}`])\s*(?:[A-Za-z_][A-Za-z0-9_]*=\S+\s+|(?:sudo|env|command|nohup|time)\s+)*"
    r"(?:[\w./-]*/)?gh\s+(?:"
    r"issue\s+(?:create|comment|edit|close|reopen)"
    r"|pr\s+(?:create|comment|edit|review|merge|close)"
    r"|release\s+(?:create|edit)"
    r"|gist\s+create"
    r"|api\b[^|;&]*?(?:-X\s*(?:POST|PATCH|PUT|DELETE)|--method\s*(?:POST|PATCH|PUT|DELETE))"
    r")", re.M | re.I)

if not PUBLISH.search(cmd):
    sys.exit(0)

# ── Сторожевой таймер: ЕДИНСТВЕННОЕ отступление от fail-open, названное вслух ──────
# Шапка файла обещает fail-open по ошибкам разбора, и для чужого JSON или отсутствующего
# списка слов это верно: страж не понял вход или ему нечего защищать. Здесь не тот случай.
# Ниже этой строки вход ПОНЯТ и команда уже опознана как публикация — ставка ровно та,
# ради которой страж существует.
#
# ЗАМЕРЕНО (не выведено из документации): превышение таймаута хука PreToolUse
# ПРОПУСКАЕТ вызов, а не блокирует его. Контрольный опыт: хук с кодом 2 вызов
# заблокировал; хук, спящий дольше таймаута, — вызов выполнился, побочный эффект
# состоялся. Значит любой способ ЗАМЕДЛИТЬ стража есть способ его ОБОЙТИ, причём молча:
# в журнале не останется ни блокировки, ни ошибки. Документация Claude Code об исходе
# при таймауте не говорит вовсе — поэтому здесь замер, а не ссылка.
#
# Смысл таймера — сделать исход ВЫБОРОМ СТРАЖА, а не средой: не уложились в срок, значит
# НЕ ЗНАЕМ, есть ли в тексте запретное, а необратимая публикация дороже лишнего разбора.
# ИНВАРИАНТ: дедлайн обязан быть СТРОГО МЕНЬШЕ таймаута хука (у потребителей он 10 с),
# иначе исход снова определяет среда — то есть пропуск. Ручка нужна тестам и проекту с
# иным таймаутом.
_DEADLINE = int(os.environ.get("PRIVATE_WORDS_GUARD_DEADLINE", "5") or 5)


def _on_timeout(signum, frame):
    sys.stderr.write(
        "Публикация остановлена: проверить текст за отведённое время не удалось.\n\n"
        f"  Страж не уложился в {_DEADLINE} с и прервал сам себя.\n\n"
        "Это НЕ означает, что в тексте есть запретное, — это означает, что мы не знаем.\n"
        "Отправить непроверенным нельзя: публикация необратима. Сократи текст или разбей\n"
        "его на части и повтори.\n")
    sys.stderr.flush()
    os._exit(2)


if _DEADLINE > 0:
    signal.signal(signal.SIGALRM, _on_timeout)
    signal.alarm(_DEADLINE)

# ТЕСТОВАЯ ЗАКЛАДКА, названная вслух. После ограничения шаблона почты сверху ни один
# реалистичный вход стража уже не замедляет (замер: 40 КБ — 0,08 с, 360 КБ против списка
# в 60 000 слов — 0,12 с), то есть таймер выше нечем вызвать. Оставить его непроверенным
# нельзя: неработающий сторожевой таймер выглядит точно так же, как работающий, пока не
# случится настоящий откат, — это и есть чинимый здесь класс дефекта.
# Закладка БЕЗОПАСНА ПО ПОСТРОЕНИЮ: единственный её эффект — замедление, а замедление
# ведёт к БЛОКИРОВКЕ. Обойти стража через неё нельзя, можно только остановить публикацию.
if os.environ.get("PRIVATE_WORDS_GUARD_TEST_SLOW"):
    import time
    time.sleep(float(os.environ["PRIVATE_WORDS_GUARD_TEST_SLOW"]))

# Пути, которые прочитать не удалось. Пустой список = читать было нечего или всё прочли.
blocked_reads = []

haystack = cmd
# Текст может лежать в файле: `--body-file x.md`, `--notes-file y.md`. Читаем и его —
# иначе страж проверял бы имя файла вместо содержимого и пропускал ровно тот путь,
# которым удобнее всего отправить длинный текст.
#
# КОРОТКИЕ ФОРМЫ ФЛАГА досматриваются наравне с длинными. Прежде выражение знало только
# `--body-file`/`--notes-file`, и замер дал обход одной буквой: `-F dirty.md`,
# `--input dirty.md` проходили с кодом 0. При этом `gh api --input` уже числился каналом
# публикации в PUBLISH выше — то есть канал признан публикующим, а содержимое его не
# читалось. `(?<!\S)` не даёт `-F` совпасть внутри слова; длинные формы в альтернации
# стоят первыми, иначе `--file` съел бы префикс.
FILE_FLAGS = re.compile(r"(?<!\S)(?:--body-file|--notes-file|--input|--file|-F)(?:=|\s+)(\S+)")
MAX_BODY_BYTES = 1024 * 1024                 # GitHub всё равно режет тело задолго до мегабайта

for m in FILE_FLAGS.finditer(cmd):
    path = m.group(1).strip("\"'")
    if path == "-":
        continue                             # stdin: содержимое уже внутри команды (heredoc)
    if "=" in path:
        # Форма `gh api -F key=@file` / `-F key=value`. Значение без `@` инлайновое — оно
        # уже в `cmd`, то есть в haystack, читать нечего.
        key, _, value = path.partition("=")
        if not value.startswith("@"):
            continue
        path = value[1:]
    if path == "-":
        continue
    try:
        st = os.stat(path)
    except OSError:
        continue                             # файла нет — не наша забота, пусть решает gh
    # ПРОВЕРКА ТИПА ДО ОТКРЫТИЯ, а не таймер после. Путь полностью подконтролен тому, чью
    # команду проверяют, и `open()` на именованном канале висит вечно: замер показал, что
    # хук не завершился за 12 с при таймауте 10 с. Правильнее НЕ НАЧИНАТЬ чтение, чем
    # прерывать его. Исход — блокировка, а не пропуск: содержимое мы не прочли, значит
    # НЕ ЗНАЕМ, есть ли там запретное, а необратимая публикация дороже лишнего разбора.
    if not stat.S_ISREG(st.st_mode):
        blocked_reads.append(f"{path} — не обычный файл")
        continue
    if st.st_size > MAX_BODY_BYTES:
        blocked_reads.append(f"{path} — больше {MAX_BODY_BYTES // 1024} КБ")
        continue
    try:
        with open(path, encoding="utf-8", errors="replace") as fh:
            haystack += "\n" + fh.read(MAX_BODY_BYTES)
    except Exception:                        # noqa: BLE001
        blocked_reads.append(f"{path} — прочитать не удалось")

low = haystack.lower()
found = sorted({w for w in words if w.lower() in low}, key=str.lower)

# ── Шаблоны: то, что списком слов не выражается ─────────────────────────────
# Ключ, почта и телефон каждый раз РАЗНЫЕ — перечислить их нельзя, узнаётся формат.
# Шаблоны живут в КОДЕ, а не в приватном файле, и это осознанно: формат токена —
# публичное знание (его печатает сам провайдер), скрывать в нём нечего, а вот
# версионировать полезно — тогда правка формата проходит ревью и тест.
#
# Это НЕ полноценный детектор секретов: он знает ровно те формы, что перечислены.
# Главный слой против ключей — push protection GitHub: он знает форматы сотен
# провайдеров и обновляет их сам. Здесь — дешёвый упреждающий слой: ловит ДО
# отправки, когда текст ещё можно молча поправить, не оставляя следа в истории.
PATTERNS = [
    ("токен GitHub", r"\b(?:ghp|gho|ghu|ghs|ghr)_[A-Za-z0-9]{36,}\b"),
    ("токен GitHub (fine-grained)", r"\bgithub_pat_[A-Za-z0-9_]{40,}\b"),
    ("ключ OpenAI/Anthropic", r"\b(?:sk|sk-ant)-[A-Za-z0-9_-]{24,}\b"),
    ("ключ AWS", r"\b(?:AKIA|ASIA)[0-9A-Z]{16}\b"),
    ("приватный ключ", r"-----BEGIN\s+(?:[A-Z ]+\s+)?PRIVATE KEY-----"),
    ("токен Slack", r"\bxox[baprs]-[A-Za-z0-9-]{10,}\b"),
    # Адрес: слева от `@` обязателен «настоящий» локальный кусок, справа — домен.
    # Два отсева, оба найдены замером на живых проектах:
    #   • python-декораторы (`@pytest.mark.skip`, `@router.patch`) — у них слева от `@`
    #     пусто или знак пунктуации, поэтому требуем букву/цифру непосредственно перед `@`;
    #   • ссылки на реестр пакетов (`registry.npmjs.org/@astrojs/…-0.3.1.tgz`) — их
    #     отсекает запрет `/` внутри домена и проверка на `://` перед совпадением.
    # Без этих отсевов замер дал 57 ложных срабатываний в одном проекте и 129 в другом —
    # то есть страж ругался бы на код и на файл блокировок, а такого снимают за неделю.
    # Повторы ОГРАНИЧЕНЫ СВЕРХУ по смыслу поля, а не «на глазок»: локальная часть адреса
    # по RFC 5321 не длиннее 64 знаков, домен — 255. Без границ два открытых жадных класса
    # вокруг `@` давали квадратичный откат на входе БЕЗ `@`: замер 5 КБ → 0,13 с,
    # 40 КБ → 8,66 с (вчетверо на каждое удвоение). При таймауте хука 10 с это не
    # медлительность, а ОБХОД: страж умирал молча, и текст уходил непроверенным.
    # Подушка набирается без злого умысла — список идентификаторов через запятую или
    # JSON в одну строку; перенос строки откат рвёт, поэтому страж отказывал ВЫБОРОЧНО.
    # Адрес с локальной частью длиннее 64 знаков невалиден, так что ловить нечего.
    ("адрес почты",
     r"(?<![\w.])(?<!://)[^\s@<>()\[\]{}\"',;/]{0,64}[A-Za-z0-9]@[^\s@<>()\[\]{}\"',;/]{1,255}\.[^\W\d_]{2,}(?![\w.])"),
    # Телефон намеренно УЗКО: только международная запись со знаком «плюс». Шаблон
    # пошире ловил бы версии, даты и хеши — а ложно блокирующего стража снимают
    # целиком вместе с пользой.
    ("номер телефона", r"(?<![\w.])\+\d{1,3}[\s(-]?\d{3}[\s)-]?\d{3}[\s-]?\d{2}[\s-]?\d{2}(?![\w.])"),
]
# Заведомо безопасные адреса: их не прячут, а вписывают намеренно. Проектные исключения
# (собственные домены, тестовые адреса) кладутся в `.claude/private-words-allow.txt` —
# рядом со списком слов и так же вне git. Без него работает только встроенный набор.
# Зачем настраиваемо: у проекта с публичным контактным адресом (`hello@…`) страж без
# исключений ругался бы на каждый документ, где этот адрес упомянут, — и был бы снят.
MAIL_OK = re.compile(
    r"(?:noreply|no-reply|example\.(?:com|org)|@anthropic\.com|localhost"
    r"|registry\.npmjs\.org|unpkg\.com|cdn\.jsdelivr)", re.I)

try:
    # `errors="replace"` + широкий перехват — по той же причине, что и у списка слов выше:
    # битый байт в файле исключений не должен отключать стража целиком.
    with open(os.environ["WORDS_FILE"].replace("private-words.txt", "private-words-allow.txt"),
              encoding="utf-8", errors="replace") as fh:
        extra_ok = [w.strip() for w in fh if w.strip() and not w.lstrip().startswith("#")]
except Exception:                            # noqa: BLE001
    extra_ok = []

for label, pattern in PATTERNS:
    for m in re.finditer(pattern, haystack):
        hit = m.group(0)
        if label == "адрес почты" and MAIL_OK.search(hit):
            continue
        if any(ok.lower() in hit.lower() for ok in extra_ok):
            continue                         # проектное исключение
        # Показываем ОБРЕЗАННО: полный секрет в тексте ошибки — это ещё одна его копия,
        # теперь в журнале сессии.
        found.append(f"{label} ({hit[:10]}…)")

# Файл тела, который прочитать НЕ УДАЛОСЬ, — это отдельный исход, и он БЛОКИРУЮЩИЙ.
# Логика та же, что у сторожевого таймера: содержимое не прочли → не знаем, есть ли там
# запретное → публиковать вслепую нельзя. Прежде такой файл молча пропускался, то есть
# страж проверял имя файла вместо содержимого — ровно тот путь, которым удобнее всего
# отправить длинный текст. Сообщение отдельное: смешать его с «нашли приватное слово»
# значило бы сказать человеку неправду о том, что произошло.
if blocked_reads and not found:
    sys.stderr.write(
        "Публикация остановлена: содержимое файла с текстом проверить не удалось.\n\n"
        "  " + "\n  ".join(blocked_reads) + "\n\n"
        "Это НЕ означает, что в файле есть запретное, — это означает, что мы не знаем.\n"
        "Страж читает только обычные файлы и только до предела размера: именованный канал\n"
