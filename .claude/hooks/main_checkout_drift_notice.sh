#!/usr/bin/env bash
# SessionStart: сказать, если то, что читает ЭТА сессия, отстало от главной папки.
#
# ЗАЧЕМ. Между слиянием в общую ветку и папкой, из которой сессия читает правила и
# стражей, нет ни одного автоматического шага: там никто не делает `git pull`. Правка
# может быть слита и при этом не действовать ни в одной сессии, и отказ полностью
# молчаливый — «страж не сработал» неотличимо от «нарушений не было».
#
# ДВЕ СВЕРКИ, ПОТОМУ ЧТО ЧИТАЮТ ИЗ ДВУХ МЕСТ.
#   1. ГЛАВНАЯ ПАПКА против origin: сессия, запущенная в ней, идёт по её коммиту.
#   2. КОПИЯ ЭТОЙ СЕССИИ против главной папки — по НАБОРУ СТРАЖЕЙ, а не по числу
#      коммитов. Отставание рабочей ветки на коммиты нормально и само по себе ничего
#      не значит; значит другое — что в этой копии стражи другие или их нет вовсе.
#      Замерено 2026-07-24 на семи рабочих копиях этого репозитория: три держали ноль
#      стражей и ни одного файла настроек, ещё две — четыре стража из шести. При этом
#      правила там свежие (доставлены ссылкой) и утверждают, что стражи работают.
#
# Поведение: только СООБЩАЕТ, ничего не тянет и не правит. Тянуть автоматически нельзя —
# в главной папке может идти чужая незакоммиченная работа.
#
# Главная папка НЕ ЗАДАНА ПУТЁМ: её спрашивают у git (`--git-common-dir`). Записанный
# путь верен ровно на одной машине, а в публичном репозитории он ещё и раскрывает её
# устройство.
#
# Fail-open во всём: нет сети, нет репозитория, нет ветки — молчим и выходим 0.
set -u

err=/dev/null
[ -n "${CHECKOUT_DRIFT_STRICT:-}" ] && err=/dev/stderr

input=$(cat 2>/dev/null || true)
HOOK_INPUT="$input" python3 - <<'PY' 2>"$err"
import filecmp
import json
import os
import subprocess
from pathlib import Path

ВЕТКА = os.environ.get("CHECKOUT_DRIFT_BRANCH", "main")


def git(каталог, *аргументы, таймаут=10):
    """stdout команды git или None. Любая беда — None: молчим, а не падаем."""
    try:
        r = subprocess.run(["git", "-C", str(каталог), *аргументы],
                           capture_output=True, text=True, timeout=таймаут)
    except Exception:
        return None
    return r.stdout.strip() if r.returncode == 0 else None


try:
    data = json.loads(os.environ.get("HOOK_INPUT", "{}"))
except Exception:
    raise SystemExit(0)
if not isinstance(data, dict):
    raise SystemExit(0)

каталог = (data.get("cwd") or "").strip()
if not каталог or not Path(каталог).is_dir():
    raise SystemExit(0)

копия = git(каталог, "rev-parse", "--show-toplevel")
общий = git(каталог, "rev-parse", "--git-common-dir")
if not копия or not общий:
    raise SystemExit(0)                      # не репозиторий — не наше дело

# `--git-common-dir` отдаёт путь относительно ТЕКУЩЕГО каталога, когда сессия идёт в
# главной папке. Приводим к абсолютному от cwd, а не от копии: иначе в подкаталоге
# получится несуществующий путь, и обе сверки молча выключатся.
общий_p = Path(общий)
if not общий_p.is_absolute():
    общий_p = (Path(каталог) / общий_p).resolve()
главная = общий_p.parent
копия_p = Path(копия).resolve()

сообщения = []

# ── 1. Главная папка против общей ветки ─────────────────────────────────────
git(главная, "fetch", "--quiet", "origin", ВЕТКА, таймаут=15)   # нет сети — не беда
отстала = git(главная, "rev-list", "--count", f"HEAD..origin/{ВЕТКА}")
if отстала and отстала.isdigit() and int(отстала) > 0:
    сообщения.append(
        f"главная папка отстала от origin/{ВЕТКА} на {отстала} коммит(ов): {главная}. "
        f"Сессия, запущенная в ней, идёт по старым правилам и старым стражам. Подтянуть "
        f"можно только после проверки, что там нет чужой незакоммиченной работы: сперва "
        f"`git -C {главная} status --short`.")

# ── 2. Набор стражей этой копии против главной папки ────────────────────────
if копия_p != главная.resolve():
    свои, главные = копия_p / ".claude", главная / ".claude"

    if (главные / "settings.json").is_file() and not (свои / "settings.json").is_file():
        сообщения.append(
            "в этой рабочей копии НЕТ .claude/settings.json — здесь не сработает ни один "
            "страж, включая проверку перед отправкой в публичный репозиторий, при том что "
            "правила утверждают обратное.")

    if (главные / "hooks").is_dir():
        эталон = {p.name: p for p in (главные / "hooks").glob("*.sh")}
        мои = {p.name: p for p in (свои / "hooks").glob("*.sh")} if (свои / "hooks").is_dir() else {}
        нет = sorted(set(эталон) - set(мои))
        разошлись = sorted(
            имя for имя in set(эталон) & set(мои)
            if not filecmp.cmp(str(эталон[имя]), str(мои[имя]), shallow=False))
        if нет:
            сообщения.append(f"стражей нет в этой копии ({len(нет)} из {len(эталон)}): "
                             + ", ".join(нет))
        if разошлись:
            сообщения.append("стражи в этой копии РАЗОШЛИСЬ с главной папкой: "
                             + ", ".join(разошлись))

if not сообщения:
    raise SystemExit(0)

print("Что читает эта сессия — устарело:")
for строка in сообщения:
    print(f" - {строка}")
print("   Пересоздать копию от свежей общей ветки надёжнее, чем доливать файлы руками. "
      "[checkout-drift]")
PY
exit 0
