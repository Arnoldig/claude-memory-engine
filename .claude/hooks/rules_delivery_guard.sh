#!/usr/bin/env bash
# SessionStart: правила проекта обязаны быть в рабочей копии — и не быть отставшей копией.
#
# Зачем. Worktree — отдельная выкладка git, и неотслеживаемые файлы в неё не попадают.
# CLAUDE.md лежит в .gitignore (репозиторий публичный, файл рабочий), поэтому в рабочую
# копию он не едет вовсе. Отказ полностью молчаливый: отсутствие правил ничем не
# отличается от их соблюдения, и заметить это можно только случайно.
#
# Механизм `.worktreeinclude` (заявка #23) закрывает дыру наполовину: он срабатывает при
# СОЗДАНИИ копии и кладёт КОПИЮ файла. Копия отстаёт молча — замерено 2026-07-20: в одной
# рабочей копии лежала редакция правил на 3 КБ короче основной, и сессия шла по
# устаревшим правилам, не имея способа это заметить. Отставшая копия хуже отсутствия:
# отсутствие хотя бы заметно. Поэтому доставка — ССЫЛКОЙ: отстать она не может.
#
# Страж чинит доставку в рабочей копии и ГОВОРИТ об этом. Молчит, когда всё на месте:
# страж, шумящий без повода, будет отключён вместе с настоящими находками.
#
# Fail-open: любая неожиданность → выход 0 без вывода. SessionStart не имеет права
# ронять старт сессии, а правила не должны мешать работе.
set -u

input=$(cat 2>/dev/null || true)
HOOK_INPUT="$input" python3 - <<'PY' 2>/dev/null
import filecmp
import json
import os
from pathlib import Path

try:
    data = json.loads(os.environ.get("HOOK_INPUT", "{}"))
except Exception:
    raise SystemExit(0)

каталог = (data.get("cwd") or "").strip()
if not каталог or not Path(каталог).is_dir():
    raise SystemExit(0)

# Рабочая копия опознаётся по своему месту: <корень>/.claude/worktrees/<имя>/...
# В самой главной папке файл настоящий, и чинить там нечего.
метка = f"{os.sep}.claude{os.sep}worktrees{os.sep}"
каталог_p = Path(каталог).resolve()
if метка not in f"{каталог_p}{os.sep}":
    raise SystemExit(0)

корень = Path(str(каталог_p).split(метка)[0])
if not (корень / "CLAUDE.md").is_file():
    raise SystemExit(0)          # нечего доставлять

# Ссылка ОТНОСИТЕЛЬНАЯ: она не содержит путей машины (репозиторий публичный) и
# переживает переименование домашней папки.
ДОСТАВЛЯЕМОЕ = (
    ("CLAUDE.md", "правила проекта"),
    (os.path.join(".claude", "private-words.txt"), "список приватных слов"),
)

сообщения = []
for относительный, имя in ДОСТАВЛЯЕМОЕ:
    источник = корень / относительный
    if not источник.is_file():
        continue
    цель = каталог_p / относительный
    каталог_цели = цель.parent
    ссылка = os.path.relpath(источник, каталог_цели)

    try:
        if цель.is_symlink():
            if not цель.exists():                       # ссылка повисла
                цель.unlink()
                цель.symlink_to(ссылка)
                сообщения.append(f"{имя}: ссылка была битой, восстановлена")
            continue

        if not цель.exists():
            каталог_цели.mkdir(parents=True, exist_ok=True)
            цель.symlink_to(ссылка)
            сообщения.append(f"{имя}: в рабочей копии не было, доставлено ссылкой")
            continue

        # Обычный файл — это копия. Она уже могла отстать, поэтому сверяем с основным.
        if filecmp.cmp(str(цель), str(источник), shallow=False):
            цель.unlink()
            цель.symlink_to(ссылка)
            сообщения.append(f"{имя}: копия заменена ссылкой, чтобы не отстала позже")
        else:
            сообщения.append(
                f"{имя}: в рабочей копии лежит КОПИЯ, РАЗОШЕДШАЯСЯ с основной — "
                f"сессия идёт по устаревшему. Основной файл: {источник}")
    except Exception:
        continue                                        # fail-open, по каждому файлу

if сообщения:
    print("Доставка правил в рабочую копию:")
    for строка in сообщения:
        print(f" - {строка}")
PY
exit 0
