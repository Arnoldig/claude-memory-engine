#!/usr/bin/env bash
# SessionStart: файлы правил в рабочей ветке — доставить или сказать, что разошлись.
#
# РЕЖИМ ВЫБИРАЕТСЯ ВОПРОСОМ К GIT, А НЕ ПО ПРОЕКТУ. Способ доставки — свойство
# ФАЙЛА, а не репозитория, и перепутанный режим стоит сломанного репозитория:
#
#   • ФАЙЛ ИГНОРИРУЕТСЯ git (здесь так лежат CLAUDE.md и списки слов) — в рабочую
#     ветку он не приедет никогда, а подменить его ссылкой безопасно: ссылке некуда
#     закоммититься. Режим: ДОСТАВИТЬ ОТНОСИТЕЛЬНОЙ ССЫЛКОЙ. Ссылка, в отличие от
#     копии, отстать не может.
#
#   • ФАЙЛ ОТСЛЕЖИВАЕТСЯ git — его кладёт сама выкладка, доставлять нечего. А ссылка
#     поверх него меняет ТИП файла (100644 → 120000): замерено в одноразовом
#     репозитории — `git status` показывает ` T`, первый же `git add -A` увозит
#     ссылку в коммит, и в чистом клоне остаётся ссылка наружу репозитория, то есть
#     текста правил нет ни у кого. Режим: ТОЛЬКО СВЕРИТЬ И СКАЗАТЬ.
#
# Правило записано КОДОМ, а не оговоркой в комментарии, намеренно: полагаться, что
# следующая сессия прочтёт оговорку, нельзя. Если git недоступен или ответ непонятен —
# считаем файл ОТСЛЕЖИВАЕМЫМ и ссылку НЕ делаем: цена лишней строки в выводе нулевая,
# цена лишней ссылки — сломанный репозиторий у всех.
#
# ЧТО СТЕРЕЖЁТСЯ
#   • CLAUDE.md — сессия читает его из своей ветки, отставший файл = старые правила;
#   • .claude/private-words.txt — без него страж публикации молчит словарной половиной,
#     а «страж молчит» неотличимо от «нарушений нет»;
#   • .claude/private-words-allow.txt — без него страж публикации в ветке СТРОЖЕ, чем
#     в главной папке: снятое там ложное срабатывание здесь возвращается.
#
# Молчит, когда всё на месте: страж, шумящий без повода, будет отключён вместе с
# настоящими находками. Разошедшийся файл не затирает, а называет — расхождение может
# быть чьей-то незакоммиченной работой.
#
# Fail-open во всём: любая неожиданность → выход 0 без вывода. SessionStart не имеет
# права ронять старт сессии. Молчание НЕ означает «всё чисто».
set -u

# В работе ошибки разбора глушатся: сломанный хук не должен сыпать в старт сессии.
# Цена глушения — «промолчал» и «упал» становятся неразличимы, а это ровно тот класс,
# на котором стражи умирают незаметно. Поэтому есть режим для теста: с непустым
# RULES_DELIVERY_STRICT ошибки видны в stderr, и тест требует его пустым на каждом
# случае, где страж ОБЯЗАН молчать.
#
# Имя переменной ЛАТИНИЦЕЙ обязательно: bash не принимает не-ASCII в именах, и такое
# присваивание он молча считает командой — страж перестаёт работать целиком, оставаясь
# с виду исправным. Замерено здесь же: все 19 случаев разом.
err=/dev/null
[ -n "${RULES_DELIVERY_STRICT:-}" ] && err=/dev/stderr

input=$(cat 2>/dev/null || true)
HOOK_INPUT="$input" python3 - <<'PY' 2>"$err"
import filecmp
import json
import os
import subprocess
from pathlib import Path

try:
    data = json.loads(os.environ.get("HOOK_INPUT", "{}"))
except Exception:
    raise SystemExit(0)
if not isinstance(data, dict):
    raise SystemExit(0)

каталог = (data.get("cwd") or "").strip()
if not каталог or not Path(каталог).is_dir():
    raise SystemExit(0)

# Рабочая ветка опознаётся по своему месту: <корень>/.claude/worktrees/<имя>/...
# В самой главной папке файлы настоящие, и делать там нечего.
метка = f"{os.sep}.claude{os.sep}worktrees{os.sep}"
каталог_p = Path(каталог).resolve()
if метка not in f"{каталог_p}{os.sep}":
    raise SystemExit(0)

корень = Path(str(каталог_p).split(метка)[0])
if not (корень / "CLAUDE.md").is_file():
    raise SystemExit(0)          # не тот проект / нечего сверять

# Работаем с ВЕРШИНОЙ рабочей ветки, а не с текущим каталогом: сессия может начаться
# в подкаталоге, а стражи ищут свои файлы именно в вершине. Доставка мимо неё оставила
# бы стража публикации без списка слов — то есть слепым — и отрапортовала бы об успехе.
хвост = str(каталог_p).split(метка, 1)[1]
ветка = Path(str(корень) + метка + хвост.split(os.sep)[0])

СТЕРЕЖЁМОЕ = (
    ("CLAUDE.md", "правила проекта"),
    (os.path.join(".claude", "private-words.txt"), "список приватных слов"),
    (os.path.join(".claude", "private-words-allow.txt"), "список исключений стража публикации"),
)


def отслеживается(относительный: str) -> bool:
    """Знает ли git об этом файле в рабочей ветке.

    При ЛЮБОМ сомнении отвечаем ДА: тогда страж ограничится сообщением. Ошибка в эту
    сторону стоит лишней строки в выводе, ошибка в другую — смены типа файла в git.
    """
    try:
        r = subprocess.run(["git", "-C", str(ветка), "ls-files", "--", относительный],
                           capture_output=True, text=True, timeout=5)
    except Exception:
        return True
    if r.returncode != 0:
        return True
    return bool(r.stdout.strip())


сообщения = []
расхождение = False
for относительный, имя in СТЕРЕЖЁМОЕ:
    источник = корень / относительный
    цель = ветка / относительный
    try:
        if not источник.is_file():
            continue                                    # нет эталона — не наше дело

        # ── Режим «только сказать»: файл в git, ссылку делать нельзя ──────────
        if отслеживается(относительный):
            if цель.is_symlink():
                # Читается исправно и выглядит нормально, а git уже видит смену типа.
                # Промолчать — значит дождаться коммита, который сломает файл у всех.
                сообщения.append(
                    f"{имя}: в рабочей ветке ССЫЛКА вместо отслеживаемого файла — git видит "
                    f"смену типа файла, и первый же `git add -A` увезёт её в репозиторий "
                    f"({относительный}). Вернуть настоящий файл: "
                    f"git checkout -- {относительный}")
                расхождение = True
            elif not цель.exists():
                сообщения.append(f"{имя}: в рабочей ветке НЕТ ({относительный})")
                расхождение = True
            elif not filecmp.cmp(str(цель), str(источник), shallow=False):
                сообщения.append(
                    f"{имя}: в рабочей ветке РАЗОШЁЛСЯ с главной копией ({относительный})")
                расхождение = True
            continue

        # ── Режим «доставить ссылкой»: файла в git нет, смены типа не будет ───
        каталог_цели = цель.parent
        ссылка = os.path.relpath(источник, каталог_цели)  # относительная: без путей машины

        if цель.is_symlink():
            if not цель.exists():
                цель.unlink(); цель.symlink_to(ссылка)
                сообщения.append(f"{имя}: ссылка была битой, восстановлена")
            elif цель.resolve() != источник.resolve():
                # Живая ссылка не на основной файл: сессия шла бы по постороннему тексту,
                # а всё выглядело бы исправным. Молча принимать нельзя.
                цель.unlink(); цель.symlink_to(ссылка)
                сообщения.append(f"{имя}: ссылка вела не на основной файл, исправлена")
            continue

        if not цель.exists():
            каталог_цели.mkdir(parents=True, exist_ok=True)
            цель.symlink_to(ссылка)
            сообщения.append(f"{имя}: в рабочей ветке не было, доставлено ссылкой")
            continue

        if filecmp.cmp(str(цель), str(источник), shallow=False):
            цель.unlink(); цель.symlink_to(ссылка)      # копия равна оригиналу — терять нечего
            сообщения.append(f"{имя}: копия заменена ссылкой, чтобы не отстала позже")
        else:
            сообщения.append(
                f"{имя}: в рабочей ветке лежит КОПИЯ, РАЗОШЕДШАЯСЯ с основной — "
                f"не трогаю, разберись руками ({относительный})")
            расхождение = True
    except Exception:
        continue                                        # fail-open, по каждому файлу

if not сообщения:
    raise SystemExit(0)

# Направление расхождения. «Ветка отстала» и «своя недоставленная правка» требуют разных
# действий, и гадать за читателя нельзя — спрашиваем git. Подсказка только при
# расхождении: к сообщению «доставлено ссылкой» она отношения не имеет и была бы шумом.
подсказка = ""
if расхождение:
    try:
        n = subprocess.run(["git", "-C", str(ветка), "rev-list", "--count", "HEAD..origin/main"],
                           capture_output=True, text=True, timeout=5).stdout.strip()
        if n.isdigit() and int(n) > 0:
            подсказка = f"Ветка отстала от origin/main на {n} коммит(ов) — похоже на припаркованную работу."
        elif n == "0":
            подсказка = ("Ветка от origin/main не отстала — значит это СВОЯ правка, ещё не "
                         "доставленная в главную копию.")
    except Exception:
        pass

print("Файлы правил в этой рабочей ветке:")
for строка in сообщения:
    print(f" - {строка}")
if подсказка:
    print(f"   {подсказка}")
print(f"   Главная копия: {корень} [rules-delivery]")
PY
exit 0
