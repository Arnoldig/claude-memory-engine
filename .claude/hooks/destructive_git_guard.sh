#!/usr/bin/env bash
# PreToolUse (Bash): не дать выбросить незакоммиченную работу git-командой.
#
# Зачем. Прецедент 2026-07-19 в соседнем проекте: суб-агенту поручили правку,
# он выполнил `git checkout -- .` и стёр несохранённое. Текстовое правило такой
# класс не держит — команда выполняется быстрее, чем вспоминается правило.
#
# Ключевое отличие от «просто запретить»: страж блокирует ТОЛЬКО когда в дереве
# реально есть что терять. На чистом дереве `git reset --hard origin/main` —
# законная синхронизация (так вчера подтягивали отставшую главную копию), и
# мешать ей нельзя, иначе страж начнут обходить.
#
# Проверяется и рабочий каталог сессии, и путь из `git -C <путь>` / `cd <путь>`
# внутри команды: разрушительная команда часто адресована ДРУГОМУ репозиторию.
#
# Обход для случая «владелец сказал делать»: добавить в команду комментарий
# `# согласовано с владельцем`. Обход намеренно явный и видимый в истории.
#
# Fail-open: нет git, нет пути, непонятный ввод — пропускаем. Страж не должен
# останавливать работу из-за собственной ошибки.

input=$(cat)
HOOK_INPUT="$input" python3 - <<'PY'
import json, os, re, subprocess, sys

try:
    data = json.loads(os.environ.get("HOOK_INPUT", "{}"))
except Exception:
    raise SystemExit(0)

if data.get("tool_name") != "Bash":
    raise SystemExit(0)

cmd = (data.get("tool_input") or {}).get("command") or ""
if not cmd:
    raise SystemExit(0)

# Явное согласие владельца — пропускаем, но след остаётся в самой команде.
if "согласовано с владельцем" in cmd:
    raise SystemExit(0)

# Между `git` и глаголом стоят общие опции: `git -C <путь> reset --hard`,
# `git --no-pager stash`. Без этого куска регулярка пропускала команду,
# адресованную ДРУГОМУ репозиторию, — а это самый опасный случай.
ОБЩИЕ_ОПЦИИ = r"(?:-[a-zA-Z]\s+\S+\s+|--[a-z][a-z-]*(?:=\S+)?\s+)*"

# Пропуск слов между глаголом и флагом. Был выписан отдельно в каждой строке
# списка — и расходился: одну правили, соседние забывали.
ПРОПУСК_СЛОВ = r"(?:[^\s;&|`)(]+[ \t]+)*?"

# «Силовой» флаг во ВСЕХ написаниях сразу: `-f`, слипшиеся `-fd`/`-df`, длинное
# `--force`. Раньше каждая строка списка перечисляла написания сама, и они
# разошлись зеркально: `clean` знал слипшиеся, но не знал `--force`;
# `checkout` — ровно наоборот. Замерено 2026-07-20 проверкой по последствию:
# пять команд уносили несохранённое молча. Держит
# `test_destructive_git_guard_by_effect.py`: перечень форм там не рукописный.
ФЛАГ_СИЛЫ = r"(-[a-zA-Z]*f[a-zA-Z]*\b|--force\b)"

# Команды, выбрасывающие незакоммиченные изменения рабочего дерева.
ОПАСНЫЕ = (
    (r"reset\s+--hard\b", "git reset --hard"),
    (r"checkout\s+(--\s|\.\s*$|--\s*\.)", "git checkout --"),
    # Пропуск слов НЕ пересекает границу команды. Две разные вещи, и обе нужны:
    #   • сам символ-разделитель исключён из СЛОВА — `;&|`()`;
    #   • между словами стоит `[ \t]+`, а НЕ `\s+`. Перенос строки — такой же
    #     разделитель команд, как `;`, и в набор символов слова он входить не может:
    #     он уже внутри `\s`. Глотал его именно разделитель.
    # Обе ложные тревоги замерены: `git checkout main && rm -f /tmp/x` (2026-07-19)
    # и та же команда через перенос строки (2026-07-20).
    # `-f`/`--force` у checkout и switch выбрасывают правки так же, как `--`, но
    # мимо шаблона выше: замерено 2026-07-19 — восемь разрушительных команд
    # проходили насквозь, потому что список описывал одну форму каждой.
    (r"checkout\s+" + ПРОПУСК_СЛОВ + ФЛАГ_СИЛЫ, "git checkout -f"),
    (r"checkout\s+HEAD\b", "git checkout HEAD"),
    (r"switch\s+" + ПРОПУСК_СЛОВ + r"(--discard-changes\b|" + ФЛАГ_СИЛЫ + r")",
     "git switch --discard-changes"),
    (r"restore\b", "git restore"),
    # Откладывание уносит правки из дерева — но НЕ показ отложенного. Прежний
    # `stash\b` ловил и `list`, и `show`: замерено 48 пустых блокировок за неделю.
    # Страж, мешающий смотреть, будет снят целиком — вместе с настоящей защитой.
    (r"stash\b(?!\s+(?:list|show)\b)", "git stash"),
    (r"clean\s+" + ПРОПУСК_СЛОВ + ФЛАГ_СИЛЫ, "git clean -f"),
    # `git rm -f` удаляет файл, перебивая отказ «есть локальные правки».
    (r"rm\s+" + ПРОПУСК_СЛОВ + ФЛАГ_СИЛЫ, "git rm -f"),
    # Удаление рабочей копии с несохранённым в ней.
    (r"worktree\s+remove\s+" + ПРОПУСК_СЛОВ + ФЛАГ_СИЛЫ, "git worktree remove --force"),
)
найдено = [
    имя for шаблон, имя in ОПАСНЫЕ
    if re.search(r"git\s+" + ОБЩИЕ_ОПЦИИ + шаблон, cmd)
]

# ── Снятие стражей: блокируем НЕЗАВИСИМО от состояния дерева ──────────────────
# Эти команды сами ничего не теряют, поэтому проверка «есть что терять» их
# пропускала. Но они снимают защиту, после чего теряется что угодно:
#   • `core.hooksPath` отключает проверку перед отправкой в публичный репозиторий;
#   • алиас подменяет глагол, и опасная команда приходит под чужим именем;
#   • `core.pager`, `diff.external`, `core.sshCommand`, `core.fsmonitor` выполняют
#     произвольную команду — в том числе через безобидный на вид `git -c … log`.
# Чтение настроек (`--get`, `--list`, `--get-regexp`) не трогаем: оно ничего не меняет.
СНИМАЮЩИЕ_ЗАЩИТУ = (
    (r"git\s+(?:[^\s;&|]+\s+)*?config\b(?![^;&|]*--(?:get|list|get-regexp|get-all))",
     "git config (запись настройки)"),
    (r"git\s+-c\s+(?:core\.(?:pager|sshCommand|fsmonitor|hooksPath|editor)|"
     r"diff\.external|alias\.)", "git -c (подмена настройки на лету)"),
)
# ── БЕЛЫЙ СПИСОК: при непустом дереве разрешены только перечисленные формы ────
#
# Перечень ЗАПРЕЩЁННОГО протекал трижды подряд: пропущенные написания флагов, потом
# пять глаголов, которых не было вовсе, потом одиннадцать обходов через оболочку из
# двадцати четырёх. Это свойство приёма: список опасного пополняется после каждой
# новой потери, то есть всегда задним числом.
#
# Перечень РАЗРЕШЁННОГО конечен по построению. Замер по журналам сессий: 8431 вызов
# git, 85 различных глаголов, двадцать из них покрывают 90% работы. Пробел здесь даёт
# видимую блокировку (снимается за минуту), а не молчаливую утрату.
#
# Главный выигрыш — направление ошибки разбора. Раньше «не разобрал» означало пропуск,
# и подстановка глагола была обходом. Теперь «не разобрал» означает блок.
ЧИТАЮЩИЕ_ГЛАГОЛЫ = (
    "status", "log", "diff", "show", "rev-parse", "rev-list", "merge-base",
    "ls-files", "ls-remote", "ls-tree", "check-ignore", "describe", "blame",
    "for-each-ref", "cat-file", "symbolic-ref", "shortlog", "grep", "remote",
    "whatchanged", "count-objects", "verify-commit", "help", "version",
)
МУТИРУЮЩИЕ_БЕЗОПАСНЫЕ = (
    # Меняют репозиторий, но НЕ выбрасывают несохранённое: git сам отказывается,
    # когда затёр бы локальные правки. Проверено стендом по последствию.
    "add", "commit", "push", "fetch", "pull", "merge", "rebase", "cherry-pick",
    "revert", "tag", "init", "clone", "fsck", "bundle", "archive",
)
# `apply` намеренно НЕ здесь: с ключом `-R` он снимает правки, то есть теряет работу.
# `gc` тоже: с `--prune` он уносит уже недостижимые снимки, а на них держится
# восстановление из служебных ссылок.
СОСТАВНЫЕ_РАЗРЕШЁННЫЕ = (
    # `branch -D` уносит коммиты, но не рабочее дерево, и коммиты достаются из
    # журнала ссылок. Граница «что считаем несохранённым» проведена по рабочему
    # дереву, отложенному и игнорируемым файлам — закреплено прежним тестом.
    r"branch",
    r"rm\s+(?:[^\s;&|]+\s+)*--cached",                  # снимает с учёта, файл остаётся
    r"stash\s+(?:push|list|show|apply|pop|save)",       # без drop и clear
    r"worktree\s+(?:list|add)",                          # без remove
    # Переключение ветки: git сам откажется, если правки не переносятся. Опасны
    # только принудительные формы, и они остаются в перечне запрещённого выше.
    r"switch(?!\s+(?:[^\s;&|]+\s+)*(?:--discard-changes|-[a-zA-Z]*f\b|--force))",
    r"checkout\s+(?:[^\s;&|]+\s+)*-b",                   # то же
    r"config\s+(?:[^\s;&|]+\s+)*--(?:get|list|get-regexp|get-all)",
    r"submodule\s+(?:status|init|update(?!\s+(?:[^\s;&|]+\s+)*-[a-zA-Z]*f))",
)

_разрешено = "|".join(
    list(ЧИТАЮЩИЕ_ГЛАГОЛЫ) + list(МУТИРУЮЩИЕ_БЕЗОПАСНЫЕ) + list(СОСТАВНЫЕ_РАЗРЕШЁННЫЕ))
РАЗРЕШЁННАЯ_ФОРМА = re.compile(
    r"^\s*(?:" + _разрешено + r")(?:\s|$)", re.I)


def _разрешённый_checkout(остаток: str, пути_репо: list) -> bool:
    """`checkout ВЕТКА` правок не теряет, `checkout ФАЙЛ` — теряет. Спрашиваем git.

    Различить их текстом нельзя: имя ветки и имя файла выглядят одинаково, а `--`
    отделяет пути не всегда. Поэтому решает сам git, а любая двусмысленность —
    существует и ветка, и файл с таким именем — трактуется как потеря.
    """
    слова = остаток.split()
    if "--" in слова:
        return False                          # `checkout -- .` — всегда про пути
    # Первое слово — сам глагол, его пропускаем: иначе целью станет «checkout».
    аргументы = [a for a in слова[1:] if not a.startswith("-")]
    if not аргументы:
        return False
    цель = аргументы[0]
    for путь in пути_репо or ["."]:
        try:
            ветка = subprocess.run(
                ["git", "-C", путь, "rev-parse", "--verify", "--quiet", cel := цель],
                capture_output=True, text=True, timeout=5).returncode == 0
            файл = subprocess.run(
                ["git", "-C", путь, "ls-files", "--error-unmatch", cel],
                capture_output=True, text=True, timeout=5).returncode == 0
            if ветка and not файл:
                return True
        except Exception:
            return False                      # не смогли спросить → считаем потерей
    return False


for шаблон, имя in СНИМАЮЩИЕ_ЗАЩИТУ:
    if re.search(шаблон, cmd, re.I):
        sys.stderr.write(
            f"Заблокировано: `{имя}` снимает или обходит других стражей.\n\n"
            "Такая команда сама ничего не теряет — она отключает защиту, после чего\n"
            "теряется что угодно: проверка перед отправкой в публичный репозиторий,\n"
            "подмена глагола алиасом, выполнение произвольной команды через настройку.\n\n"
            "Что делать: если настройку правда надо поменять, сделай это сам или\n"
            "допиши в команду комментарий `# согласовано с владельцем`.\n"
            "[destructive-git-guard]\n")
        sys.exit(2)
# Какие репозитории может задеть команда: каталог сессии + пути из -C и cd.
пути = []
cwd = data.get("cwd") or ""
if cwd:
    пути.append(cwd)
пути += re.findall(r"git\s+-C\s+\"([^\"]+)\"", cmd)
пути += re.findall(r"git\s+-C\s+'([^']+)'", cmd)
пути += re.findall(r"git\s+-C\s+([^\s'\"]+)", cmd)
пути += re.findall(r"cd\s+\"([^\"]+)\"", cmd)
пути += re.findall(r"cd\s+'([^']+)'", cmd)
пути += re.findall(r"cd\s+([^\s'\";&|]+)", cmd)

# Применяем БЕЛЫЙ СПИСОК. Каждое вхождение `git` в команде обязано свестись к
# разрешённой форме; всё прочее — в `найдено`, то есть будет остановлено, если в
# дереве есть что терять. Подстановка глагола (`$G checkout`, `git $(echo …)`)
# разрешённой форме не соответствует по построению — раньше это был обход.
# Опции ПЕРЕД глаголом снимаем: `git -c user.email=… commit`, `git -C <путь> status`,
# `git --no-pager log` — законные и частые формы. Опасные настройки в `-c` (подмена
# pager, hooksPath, алиаса) блокируются выше отдельно и сюда не доходят.
ОПЦИИ_ПЕРЕД_ГЛАГОЛОМ = re.compile(
    r"^(?:(?:-c\s+[^\s;&|]+|-C\s+[^\s;&|]+|--no-pager|-P|--git-dir=[^\s;&|]+|"
    r"--work-tree=[^\s;&|]+|--exec-path(?:=[^\s;&|]+)?)\s+)+", re.I)

# `git` берём ТОЛЬКО в позиции начала команды: начало строки или после разделителя.
# Слово в середине текста — не вызов. Замерено 2026-07-20: широкая редакция ловила
# `git` внутри русской фразы в комментарии скрипта и блокировала обычную проверку.
# Цена сужения названа: `sudo git …`, `env git …` сюда не попадут — их держит
# перечень запрещённого выше, где такие префиксы разобраны отдельно.
for m in re.finditer(r"(?:^|[;&|`)(]|&&|\|\|)\s*git\s+([^;&|]*)", cmd):
    остаток = ОПЦИИ_ПЕРЕД_ГЛАГОЛОМ.sub("", m.group(1).strip()).strip()
    if not остаток:
        continue
    if РАЗРЕШЁННАЯ_ФОРМА.match(остаток):
        continue
    if re.match(r"^checkout\b", остаток, re.I) and _разрешённый_checkout(остаток, пути):
        continue
    глагол = (остаток.split() or ["?"])[0]
    найдено.append(f"git {глагол} (нет в перечне разрешённых)")

# Имя `git`, положенное в переменную, и глагол через подстановку: свести к
# разрешённой форме нельзя, значит содержимое неизвестно до выполнения — это блок.
#
# ГРАНИЦА НАЗВАНА ВСЛУХ: слово `git` внутри кавычек здесь НЕ ловится. Ловить его
# значило бы блокировать любой скрипт, вызывающий git списком аргументов, — в том
# числе собственные тесты этого репозитория. Замерено 2026-07-20: правило в широкой
# редакции заблокировало обычную проверку. Такой обход закрывает не перечень, а
# снимок работы (`work_snapshot.sh`): он делается до команды независимо от её вида.
if re.search(r"(?:^|[;&|]|\s)[A-Za-z_][A-Za-z0-9_]*=git(?:\s|;|$)", cmd) or re.search(
        r"\$\{?[A-Za-z_][A-Za-z0-9_]*\}?\s+(?:checkout|reset|clean|restore|stash|rm)\b", cmd):
    найдено.append("git через подстановку (глагол неизвестен до выполнения)")

if not найдено:
    raise SystemExit(0)

def грязное(путь: str) -> str:
    """Возвращает краткую сводку несохранённого или пустую строку."""
    try:
        r = subprocess.run(
            ["git", "-C", путь, "status", "--porcelain"],
            capture_output=True, text=True, timeout=10,
        )
    except Exception:
        return ""
    if r.returncode != 0:
        return ""
    строки = [s for s in r.stdout.splitlines() if s.strip()]
    # Неотслеживаемые файлы теряет только `git clean`; остальные команды их не трогают.
    if "git clean -f" not in найдено:
        строки = [s for s in строки if not s.startswith("??")]

    # ИГНОРИРУЕМЫЕ файлы обычная проверка не показывает вовсе, и «не увидели»
    # читается как «нечего терять». Между тем именно там лежит самое дорогое:
    # список приватных слов держат ВНЕ git намеренно, восстановить его нечем, а без
    # него молча выключается защита от утечки в публичный репозиторий. Замерено
    # 2026-07-20: `git clean -xfd` уносил его при полностью чистом дереве.
    # Спрашиваем только когда команда до них добирается: у `clean` есть ключ `-x`/`-X`.
    if re.search(r"clean\s+(?:[^\s;&|]*\s+)*?-[a-zA-Z]*[xX]", cmd):
        try:
            i = subprocess.run(
                ["git", "-C", путь, "status", "--porcelain", "--ignored"],
                capture_output=True, text=True, timeout=10,
            )
            строки += [s for s in i.stdout.splitlines() if s.startswith("!!")]
        except Exception:
            pass

    # ОТЛОЖЕННОЕ рабочее дерево не показывает вовсе. Команды, которые уносят именно
    # его, при чистом дереве проходили молча: «не увидели» читалось как «нечего
    # терять» — тот самый молчаливо пустой разбор. Замерено 2026-07-20: работа,
    # отложенная и удалённая, не восстанавливается ничем.
    if re.search(r"stash\s+(?:drop|clear|pop)\b", cmd):
        try:
            s = subprocess.run(["git", "-C", путь, "stash", "list"],
                               capture_output=True, text=True, timeout=10)
            отложено = [x for x in s.stdout.splitlines() if x.strip()]
        except Exception:
            отложено = []
        строки += ["  отложено: " + x for x in отложено[:12]]

    return "\n".join("  " + s for s in строки[:12]) if строки else ""

for путь in dict.fromkeys(пути):
    сводка = грязное(путь)
    if not сводка:
        continue
    print(
        f"Заблокировано: `{найдено[0]}` выбросит незакоммиченную работу.\n\n"
        f"Репозиторий: {путь}\n"
        f"Пропадёт:\n{сводка}\n\n"
        "Это может быть работа параллельной сессии — не только твоя.\n\n"
        "Что делать: сохранить коммитом, либо убедиться, что содержимое совпадает\n"
        "с существующим коммитом (тогда терять нечего), либо спросить владельца.\n"
        "Если владелец решение подтвердил — допиши в команду комментарий\n"
        "`# согласовано с владельцем`, и она пройдёт.\n"
        "[destructive-git-guard]",
        file=sys.stderr,
    )
    raise SystemExit(2)

raise SystemExit(0)
