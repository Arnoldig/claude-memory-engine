#!/bin/bash
# PreToolUse (matcher: Bash): БЛОКИРУЕТ `gh issue close` без основания закрытия.
#
# Зачем в ЭТОМ репозитории. Заявка закрывается не фактом правки, а разбором: почему
# дефект настоящий, чем это доказано и что теперь его не пустит обратно. Без основания
# закрытая заявка через месяц неотличима от брошенной, и следующий разбор начинается с
# нуля — ровно тот класс, против которого заведён `self_check`: отсутствие сигнала
# выглядит как «всё в порядке».
#
# ПРОИСХОЖДЕНИЕ: собран из двух живых хуков, и намеренно не копирует ни один целиком —
# у каждого есть слепое пятно, которое закрывает другой.
#   • ЧеКи `issue_close_basis_guard.sh` — жёсткий deny и вырезание кавычек ПЕРЕД
#     детектом. Его дыра: наличие основания ищется по ВСЕЙ команде, поэтому
#     `wc -c f && gh issue close 5` проходит — `-c` посчитан за флаг основания.
#   • Lilya `issue_close_reminder.sh` — мягкое напоминание, но флаг ищет только в
#     хвосте аргументов САМОГО close и знает про соседний `gh issue comment`. Его
#     дыра: кавычки не вырезаются, а разделитель внутри строки засчитывается за
#     границу команды — `grep "foo; gh issue close" file` ловится ложно.
# Здесь взяты обе защиты: маскировка кавычек (от ложного ДЕТЕКТА) + хвост аргументов
# (от ложного ПРОПУСКА). Маскировка сохраняет ДЛИНУ, иначе смещения поехали бы и хвост
# отрезался не там.
#
# ВЕРДИКТ ВЫНОСИТ PYTHON, а не `RESULT=$(...)` вокруг него — как у Lilya и в отличие от
# ЧеКи. Причина не в стиле: внутри `$( )` bash ищет пару литеральной обратной кавычке
# ДАЖЕ в кавычённом heredoc, а она стоит в классе символов-разделителей `[;&|(){}`]`.
# Первая версия этого файла была написана с подстановкой и слегла с `unexpected EOF
# while looking for matching`. Хуже самой поломки было то, как она выглядела: bash
# отдаёт на синтаксической ошибке код 2 — тот же, которым PreToolUse ОТКЛОНЯЕТ вызов.
# Полностью сломанный страж неотличим от исправно работающего, и тесты «блокирует ли он
# что надо» проходили зелёными на трупе. Отсюда правило: у стража обязан быть тест на
# ПРОПУСК, иначе проверяется только то, что он умеет говорить «нет».
# Регрессия закрыта: tests/test_issue_close_basis_guard.py.
#
# ГРАНИЦА, НАЙДЕННАЯ НА СОБСТВЕННОЙ ЗАЯВКЕ #6 (2026-07-18). Этот страж стережёт КОМАНДУ
# `gh issue close` и по построению не видит ничего другого. Заявку же закрывают шестью
# путями: ключевым словом в сообщении коммита, ключевым словом в описании PR, кнопкой в
# вебе, из мобильного приложения, через REST/GraphQL, ботом. Заявка #6 закрылась при
# вливании PR (`Resolves #6` стояло и в теле squash-коммита, и в описании PR), команда
# закрытия не выполнялась вовсе — страж не вызывался, основание дописывали руками.
# Сторожить каждый канал отдельно — гонка, которую не выиграть, и две точки не
# сторожатся принципиально: `gh pr create` без флагов (текст пишется в редакторе, его
# ещё нет) и `gh pr merge --squash` без `--subject/--body` (сообщение сочиняет сервер).
# Поэтому рядом стоит НЕ ещё один страж канала, а проверка РЕЗУЛЬТАТА:
# `issue_close_basis_audit.sh` (Stop) спрашивает у GitHub, кто закрыл заявку, и требует
# основание там, где закрыл не человек. Правя этот файл, помните: он отвечает за
# командный путь и только за него.
#
# ИЗВЕСТНОЕ ЛОЖНОЕ СРАБАТЫВАНИЕ, названное вслух. Маскировка кавычек — эвристика, а не
# разбор shell-грамматики: на ВЛОЖЕННЫХ кавычках (`"… 'gh issue close 5 --comment "x"' …"`)
# пары схлопываются не по правилам shell, и фраза может выйти из-под маски. Ловится это
# на командах, которые СОДЕРЖАТ примеры закрытия, — тестовых обвязках и документации.
# Поймано на собственном тестовом скрипте при разработке 2026-07-18. Не чиню полным
# разбором осознанно: цена ошибки мала (добавь --comment либо вынеси скрипт в файл), а
# полноценный парсер shell в хуке — источник дефектов худших, чем те, что он закрыл бы.
# Тот же класс есть и у обоих хуков-источников.
input=$(cat)

HOOK_INPUT="$input" python3 - <<'PY'
import json, os, re, sys

try:
    data = json.loads(os.environ.get("HOOK_INPUT", "{}"))
except Exception:
    sys.exit(0)                              # fail-open: чужой ввод не повод падать

if (data.get("tool_name") or "") != "Bash":
    sys.exit(0)
cmd = (data.get("tool_input") or {}).get("command", "") or ""
if not cmd:
    sys.exit(0)

def mask_quotes(s: str) -> str:
    """Содержимое кавычек → заполнитель ТОЙ ЖЕ длины (смещения обязаны совпадать)."""
    def repl(m):
        body = m.group(0)
        return body[0] + "\x00" * (len(body) - 2) + body[-1]
    s = re.sub(r'"[^"]*"', repl, s)
    return re.sub(r"'[^']*'", repl, s)

masked = mask_quotes(cmd)

# Командная позиция: начало строки или разделитель, затем необязательные обёртки
# (VAR=val, sudo, env, …) и необязательный путь к бинарю (/opt/homebrew/bin/gh).
PRE = r"(?:[A-Za-z_][A-Za-z0-9_]*=\S+\s+|(?:sudo|env|command|nohup|time|setsid|doas)\s+)*"
GH = r"(?:[\w./-]*/)?gh"
SEP = r"(?:^|[;&|(){}`])\s*"
close_re = re.compile(SEP + PRE + GH + r"\s+issue\s+close\b", re.M)
m = close_re.search(masked)
if not m:
    sys.exit(0)

# Основание уже есть? Флаг ищем ТОЛЬКО в хвосте аргументов самого close — до
# следующего разделителя или подстановки. Иначе `-c` соседней команды сойдёт за него.
tail = re.split(r"[;&|\n]|\$\(|`", masked[m.end():], maxsplit=1)[0]
if re.search(r"(?:^|\s)(?:--comment(?:=|\s)|-c(?:=|\s))", tail):
    sys.exit(0)

# ЛИБО основание отдельной командой рядом — это законный второй путь.
if re.search(SEP + PRE + GH + r"\s+issue\s+comment\b", masked, re.M):
    sys.exit(0)

sys.stderr.write("""Закрытие заявки заблокировано: нет основания.

Заявка тут закрывается разбором, а не фактом правки. Без основания через месяц
закрытая заявка неотличима от брошенной.

Если ПОЧИНЕНО кодом — основание это:
  версия, в которой починено; ссылка на PR; чем доказано (число тестов, ловится ли
  теперь исходное воспроизведение, что показала проверка на живых данных); что не
  пустит дефект обратно (какой тест закрепил поведение).
  Пример: gh issue close 5 --comment "починено в 0.12.0 (#7): новая жалоба
  memory_dir_divergent; 410 тестов, исходное воспроизведение ловится, на двух живых
  конфигах молчит; инвариант закреплён тестом"

Если закрыто БЕЗ кода — основание это ДОВОД:
  дубль #<N> / так задумано, потому что <довод> / не воспроизводится на <версия>.
  «не актуально» и «устарело» основанием не считаются — они не объясняют ничего.

Второй законный путь: оставить основание отдельной командой gh issue comment.

Добавь --comment с основанием и повтори.
""")
sys.exit(2)
PY
exit $?
